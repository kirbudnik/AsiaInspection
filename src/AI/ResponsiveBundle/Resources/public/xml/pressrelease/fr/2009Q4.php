<h1>Baromètre AsiaInspection T4 2009 : Avec l’embellie économique, la conscience sociale revient</h1>
</div>
<div class="head-small">
<h2>Les pays importateurs guident la Chine sur le chemin de la responsabilité sociale</h2>
</div>

<p>
Les chiffres du Contrôle Qualité en Asie constituent assurément un bon indicateur de la santé du Commerce International. A partir de quelques indicateurs choisis, AsiaInspection publie ce baromètre de façon trimestrielle. Avec 9500 Inspections réalisées au 4ème trimestre 2009 (+ 21% vs T4 2008) pour le compte de plus de 4000 importateurs, AsiaInspection a relevé des tendances très instructives.
</p>

<p>
Une étude sur les conditions de travail en Chine réalisée fin 2009 par le gouvernement chinois a révélé certains faits préoccupants : 80% des entreprises privées chinoises violent le droit du travail en ne fournissant pas, par exemple, de contrat de travail.
</p>

<p>
Parallèlement, les audits d'éthique ont grimpé de +75% au T4 2009 à comparer aux +8% du T3 2009. Cette croissance reflète le désir grandissant des consommateurs pour plus de justice sociale. Ceci indique aussi que les consommateurs et les organisations internationales peuvent influencer les décisions des importateurs.
</p>

<p>
Une augmentation des Audits d’Usine de +66% par rapport au T3 2008 indique que les consommateurs continuent de réclamer des produits de qualité. Les entreprises important d’Asie font de la qualité une priorité pour protéger leur marque. Cette attitude a permis un T4 quasiment exempt de scandales «Made in China».
</p>

<p>
Les fêtes de fin d’année affichent une croissance à deux chiffres du nombre d’inspections pour une majorité de produits. Les inspections de jouet et d’électronique ont augmenté respectivement de +56 % et +42%. Les inspections de textile sont aussi en hausses de +67%, indiquant que les mesures chinoises de remises de taxes de début 2009 se sont avérées efficaces.
</p>


<div class="head-small">
<h2>Les pays importateurs poursuivent leur lent rétablissement post-crise</h2>
</div>
<div>
<div>
<p>
A l’instar du T3 2009, l’Asie et le Moyen Orient continuent de tirer l’économie mondiale avec respectivement +81% et +137% d’augmentation du nombre d’Inspections commandées en Asie. Les Etats-Unis et le Canada se remettent plus doucement avec +1,8% par rapport au T4 2008 et un volume stable par rapport au trimestre précédent. L’Europe de l’Est ne présente toujours pas de signe de reprise avec -37% de commandes. L’Europe de l’Ouest se redresse remarquablement avec une croissance de +26%.
</p>

