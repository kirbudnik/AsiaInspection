<h1>Baromètre AsiaInspection T3 2009 : Les exportations chinoises de nouveau à la mode</h1>
</div>
<br/>
<div class="head-small">
<h2>La production chinoise d’articles de mode est à nouveau tendance</h2>
</div>
<div class="para">
<p>
Les chiffres du Contrôle Qualité en Asie constituent assurément un bon indicateur de la santé du Commerce International. A partir de quelques indicateurs choisis, AsiaInspection publie ce baromètre de façon trimestrielle.
</p>
<p>
	Avec 9000 Inspections réalisées au 3ème trimestre 2009 (+ 32% vs T3 2008) pour le compte de plus de 3500 Importateurs, AsiaInspection a relevé des tendances très instructives.
</p>

</div>
<div id="block">
<div id="picture">
<img width="300" align="left" src="https://s3.asiainspection.com/images/news/fr_services_2009Q3.jpg" style="padding-right:15px;">
</div>
<div id="block-para">
<p>
Pour stimuler son économie, le gouvernement chinois a consenti des déductions de taxe à l’exportation. Les résultats enregistrés par AsiaInspection sont positifs et le nombre d’Inspections en hausse : les livres et la papeterie 24%, les jouets 32%, les chaussures et accessoires de mode 58% et le textile/habillement 63%. Les excellents chiffres de la confection, entre T3 2008 et T3 2009, s’expliquent également par l’ouverture totale du marché du textile Européen (janvier 2009).
</p>
</div>
<div style="clear:both;"></div>
</div>
<div class="para">
<p>
L’augmentation des Audits d’Usines de 53% est le signe d’une demande grandissante des consommateurs pour des produits de qualité. Les entreprises effectuant leur sourcing à l’étranger, plus particulièrement en Chine, sont désormais préoccupées par les enjeux d’images liés à la qualité des produits.
</p>
<p>
Le gouvernement chinois a récemment dépensé 7 millions de yuans pour améliorer la surveillance des médicaments et de l’alimentaire. Il s’agit d’une habile mesure pour rassurer les consommateurs et par la même occasion relever la réputation des exportateurs chinois - mise à mal par de récents scandales.
</p>
<p>
Les Audits Sociaux, avec une croissance de 8%, ne suivent pas la tendance des Audits d’Usines. Ce constat semble indiquer que les importateurs sont moins concernés par les enjeux de responsabilité sociale.
</p>
</div>
<div class="head-small">
<h2>Les pays exportateurs soutiennent l’économie mondiale</h2>
</div>
<div class="para">
<p>
Bien que la consommation des petits appareils électroniques ait été durement touchée, les Inspections de ce type de produit ont augmenté de 21% en T3 indiquant une reprise possible.
</p>
<p>
Quant aux pays importateurs, l'Europe de l'Est continue d'être affaiblie; les commandes de Contrôles Qualité en Asie y ont chuté de 54% au dernier trimestre, tandis que l'Europe de l'Ouest a connu une augmentation de ses commandes de 29%. L’économie américaine se stabilise et enregistre un timide +1,8% dans l’utilisation de nos services par rapport à T3 2008. Les pays émergents d'Asie et du Moyen Orient continuent d'afficher de forts besoins en contrôle qualité, avec des augmentations conséquentes du nombre d'Inspections commandées : respectivement 81% et 123%.
</p>
