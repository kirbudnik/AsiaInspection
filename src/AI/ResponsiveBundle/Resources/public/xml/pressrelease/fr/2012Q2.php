<div id="head">
	<h1>Baromètre AsiaInspection du deuxième trimestre 2012 : l’élan positif des échanges commerciaux entre la Chine et l'Afrique.</h1>
</div>
<div class="para">
	<p>
Shenzhen, Chine, le 9 Juillet 2012 - AsiaInspection, chef de file dans le domaine du contrôle qualité pour les entreprises importatrices de produits asiatiques, a annoncé aujourd'hui la publication de son baromètre pour le deuxième trimestre 2012. Le baromètre trimestriel AsiaInspection propose un synopsis d’indicateurs industriels et commerciaux liés à l’activité du contrôle qualité en Asie.
	</p>
</div>
<div class="head-small">
	<h2>L'Afrique : une destination en plein essor pour les produits
« Made in China »</h2>
</div>
<div id="block">
	<div id="block-para">
		<p>
			Les chiffres du baromètre d'AsiaInspection pour le deuxième trimestre indiquent que l'Afrique s'impose comme un importateur grandissant de produits fabriqués en Asie, et plus particulièrement pour ceux produits en Chine. La croissance de la demande de services de contrôle qualité en Asie émanant des importateurs africains a surpassé de plus 
de 20 % celle de toutes les autres régions, progressant de 23 % du premier semestre de 2011 au premier semestre de 2012. Selon les données d'AsiaInspection, les importateurs les plus actifs se trouvent en Afrique du Nord (Maroc, Tunisie et Égypte), en Afrique du Sud et au Nigéria ; une progression significative est également observée dans des pays moins connus, tels que le Botswana, le Ghana et l'Éthiopie.
		</p>
		<p>
Ces conclusions sont représentatives des données mondiales concernant le commerce entre la Chine et l'Afrique : les importations depuis la Chine entre 2010 et 2011 ont augmenté de 23,7 %, 
à 73 milliards de dollars. (Source : Standard Bank Group) 
Au cours des dernières années, la Chine est devenue le principal partenaire commercial de l'Afrique, avec des volumes d'échanges commerciaux dépassant 160 milliards de dollars en 2011.
		</p>
	</div>
	<div id="picture">
		<img src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/img/$file/china-africa-factory.jpg">
		<p>"Des travailleurs chinois et africains dans
une usine en Afrique"</p>
	</div>
	<div style="clear:both;"></div>
</div>
<div class="para">
	<p>
		Les informations d'AsiaInspection, qui reposent sur les produits manufacturés inspectés en Asie, révèlent que les importations africaines produites en Chine sont dominées par les produits textiles et les chaussures. Au premier semestre de 2012, ces catégories représentaient 80 % de tous les produits inspectés en Chine pour le compte d’importateurs africains. S'ils représentent un volume inférieur, les articles industriels et de construction ont connu une croissance à trois chiffres en matière de demandes d'inspections de la part des importateurs africains, reflétant la hausse des demandes d'infrastructures en Afrique.
	</p>
	<p>
		En observant le type de services de contrôle qualité sollicité par les importateurs africains se fournissant en Chine, les données suggèrent une chaîne d'approvisionnement plus mature et organisée. Ainsi entre le premier semestre de 2011 et le premier semestre de 2012, la demande africaine d'audits d'usines chinoises – c'est-à-dire l'évaluation d'une usine avant commande, en suivant le standard ISO 9000 – a plus que triplé.
	</p>
</div>
<div class="head-small">
	<h2>Le commerce sino-africain, une relation mutuellement bénéfique;
l'Afrique s'impose comme une zone de production mondiale.</h2>
</div>
<div class="para">
	<p>
Les échanges commerciaux entre la Chine et l'Afrique ne sont pas qu'une voie à sens unique pour les produits 
« Made in China ». Les analystes soutiennent qu'en 2012, la Chine deviendra la plus importante destination d'exportation pour l'Afrique – un statut principalement imputable aux exportations de ressources naturelles (carburant, minerais et métaux) destinées à être utilisées dans la production de marchandises.
	</p>
	<p>
Au-delà des ressources naturelles, les données de l'Organisation de Coopération et Développement Économiques (OCDE) révèlent également une croissance de 90 % des exportations de produits manufacturés en Afrique au cours de la dernière décennie. La part de la Chine comme marché de destination, a progressé de plus de 200 % sur la période, alors que les marchés d'exportation traditionnels de l'Afrique – tels que l'Union européenne et les États-Unis – enregistraient un recul relatif de 20 %.
	</p>
	<p>
Ces informations corroborent les tendances constatées par AsiaInspection ; un nombre croissant d'importateurs se fournissant en Asie exprime également le besoin d'un contrôle qualité en Afrique. « Au cours des dernières années, nous avons pu constater la hausse de l’intérêt des importateurs africains pour la Chine, lorsque nous les aidons à structurer leur chaîne d’approvisionnement en Asie.», déclare Sébastien Breteau, Président d'AsiaInspection. 
« Dans l’autre sens, et bien que les chiffres soient encore comparativement faibles, les données que nous recueillons confirment que l'Afrique se prépare aussi à devenir la prochaine destination d'approvisionnement en produits manufacturés – c’est d’ailleurs pour cela que nous y avons lancé nos services de contrôle qualité. »
	</p>
