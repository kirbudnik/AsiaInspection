<h1>Le nouveau défi des importateurs dans le contexte de crise: sécuriser leurs chaînes d'approvisionnement tout en maintenant les coûts au plus bas</h1>
</div>
<div class="para">
<p>
Baromètre AsiaInspection: les marchés comme la production ont beau être en chute, Inspections de produits et Audits d'usine ne cessent de croître pour autant.
</p>

<p>
Les chiffres du Contrôle Qualité en Asie constituent assurément un bon indicateur de la santé du Commerce International. A partir de quelques indicateurs choisis, AsiaInspection publie ce baromètre de façon trimestrielle.
</p>
<p>
Avec 8500 Inspections réalisées au 2ème trimestre 2009 (+ 26% vs T2 2008) pour le compte de plus de 2500 Importateurs, AsiaInspection (www.AsiaInspection.com) a relevé des tendances très instructives.
</p>
<p>
Les consommateurs veulent des produits plus sûrs et plus respectueux de l'environnement ; ainsi continue-t-on à observer un intérêt croissant des importateurs, désireux de garder à l'oil chacune des étapes de la supply chain afin d'éviter d'éventuels rappels de produits qui s'avéreraient coûteux et embarrassants. Après les nombreux scandales de produits toxiques en provenance de Chine ou d'autres pays asiatiques survenus au cours des deux dernières années, les Audits d'usine en Asie ont bondi de +61% entre T1 2008 et T1 2009, et les Inspections de produits ont quant à elles grimpé de +26%.</p>

<p>A travers le monde entier, les entreprises s'efforcent d'améliorer la qualité de leurs produits. A titre d'exemple, Wal-Mart a récemment annoncé que d'ici 2012, 95% de ses produits devraient provenir d'usines dont les performances environnementales et sociales sont au plus haut.</p>


</div>

<div class="head-small">
<h2>La Chine reste l'usine du monde</h2>
</div>
<div class="para">
<p>
D'après la répartition des ventes d'Inspections, les Etats-Unis se confirment comme la zone géographique la plus touchée par la crise pour ce qui est des importations en provenance d'Asie. Le chiffre d'affaires d'AsiaInspection réalisé avec ses clients américains a en effet chuté de -4.8% entre T2 2008 et T2 2009. A l'inverse, les ventes aux clients européens ainsi que celles aux marchés émergents asiatiques et sud-américains ont joliment augmenté malgré la crise, enregistrant des croissances respectives de +29 et +73% sur la même période.
</p>

<p>La répartition géographique des Inspections réalisées par AsiaInspection prouve quant à elle que la Chine reste indéniablement l'usine du monde, et ce malgré les dizaines de milliers de fermetures d'usines survenues depuis début 2008. Avec près de 7000 Inspections conduites en Chine au 2ème trimestre 2009, il s'agit en effet d'une croissance de +25% par rapport à T2 2008. Notons que les autres pays low-cost asiatiques ont connu une croissance encore plus rapide : le nombre d'Inspections conduites dans l'ensemble de ces pays a augmenté de +42% sur la même période.</p>

<p>A un moment où le Commerce International se contracte, ces chiffres reflètent la volonté croissante des entreprises de sécuriser leurs chaînes d'approvisionnement tout en maintenant leurs coûts au plus bas grâce à une production sous-traitée en Asie.</p>

<p>Les gouvernements tout comme les consommateurs ont fait de la qualité une priorité. Après que des millions de jouets à travers le monde ont été rappelés en 2007 et 2008, les Inspections de jouets réalisées par AsiaInspection ont connu une croissance de +42% entre T2 2008 et T2 2009.</p>

<p>AsiaInspection publiera le Baromètre T3 sur la base du même type d'indicateurs en Octobre prochain.</p>
