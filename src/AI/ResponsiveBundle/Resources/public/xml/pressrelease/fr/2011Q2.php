<div class="head-small">
<h2>Le commerce de l'Asie, unifié par la Chine pour la Chine</h2>
</div>
<div class="para">
<p>
Les chiffres de l'audit pour T2 2011 ont augmenté d'un remarquable +57 % d'une année sur l'autre, et de +29 % par rapport au T1. Selon le PDG d'AsiaInspection, Sébastien Breteau, ces chiffres indiquent que plus d'importateurs investissent dans leurs fournisseurs sur le long terme. « La croissance des services d'audit peut signifier que plus d'importateurs reconnaissent qu'il est important, sur le long terme, de contribuer à améliorer la qualité de l'opération de leurs fournisseurs, et pas seulement le produit qu'ils vont vendre le mois suivant. Évaluer la qualité du matériel d'usine, du personnel et des installations se rapproche à grands pas de l'amélioration de la qualité du produit une fois que la production démarre. »

</p>
<p>
Outre une augmentation de 14,4 % dans la catégorie solide du textile et de l'habillement, les chiffres de l'inspection d'AsiaInspection montrent que les produits qui étaient mous en 2010 ont fait un retour. Les plus grandes reprises ont été perçues dans l'alimentation et le conditionnement des aliments à hauteur de 98 %, les articles pour la maison et le jardin+66 %, la construction et les matériaux de construction +37 %.


</p>
