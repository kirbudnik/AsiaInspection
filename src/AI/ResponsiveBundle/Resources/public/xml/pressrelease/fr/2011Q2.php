<h1>Le baromètre d'AsiaInspection T2 2011 : la Chine sécurise son apprivionnement en pétrole vénézuélien avec ses exportations</h1>
</div>
<div class="para">
<p>
Shenzhen, China, 13 Juillet, 2011 – AsiaInspection, annonce aujourd'hui le Baromètre AsiaInspection T2 2011, une analyse trimestrielle des statistiques concernant les produits manufacturés et les services de contrôle qualité en Asie.
</p>
</div>
<div class="head-small">
<h2>La Chine prévoit un excédent commercial supérieur, les inspections suivent</h2>
</div>
<div class="para">
<p>
Au deuxième trimestre 2011, les chiffres d'inspections d'AsiaInspection en Chine ont augmenté de 27 % d'une année à l'autre, continuant à devancer la croissance de l'exportation qui a augmenté de 20 % par rapport aux débuts de l'année. La croissance chinoise fait suite à l'augmentation des salaires, à une inflation continue et aux troubles de la chaîne logistique en raison du tremblement de terre au Japon, au mois de mars. Les chiffres d'inspections d'AsiaInspection montrent également d'importantes augmentations dans les autres principaux marchés exportateurs d'Asie, où l'inflation est moins répandue, avec l'Indonésie, à hauteur de 75,7 %, le Bangladesh avec 65,8 % et le Vietnam +53 %.
</p>
</div>
<div class="head-small">
<h2>La Chine étend son modèle moyen-oriental « Exportations contre pétrole » à l'Amérique du Sud</h2>
</div>
<div id="block">
<div id="block-para">
<p>
Au deuxième trimestre, la demande des services d'AsiaInspection par les clients au Moyen-Orient a augmenté plus que dans toute autre région, marquant une augmentation d'une année sur l'autre de 71,3 %, ceci après plusieurs années de négociation par la Chine des « exportations pour le pétrole » dans la région, où elle achète aujourd'hui plus de pétrole et vend plus de marchandises que tout autre pays, y compris les États-Unis. (Source: Fast Company, mois de juin 2011)

</p>
<p>
La Chine applique le même modèle à travers l'Amérique du Sud, promettant au Venezuela des projets d'infrastructure, une aide et d'autres financements d'une valeur de 32 milliards USD. Le commerce chinois avec la région continue à augmenter, comme le montrent les commandes de service d'AsiaInspection par les clients sud-américains, à hauteur de 37,7 % d'une année à l'autre et de 50 % par rapport au premier trimestre.
</p>
</div>
