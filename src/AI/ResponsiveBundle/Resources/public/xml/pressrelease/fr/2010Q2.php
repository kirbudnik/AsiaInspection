<h1>Baromètre AsiaInspection T2 2010: La Chine imperturbable, l’Europe dans la tourmente.</h1>
</div>
<div class="para">
<p>
Les chiffres du Contrôle Qualité en Asie constituent assurément un bon indicateur de la santé du Commerce International. Avec 11 000 <a href="/services-controle-qualite-chine-inde-asie#product_inspections">Inspections</a>, <a href="/services-controle-qualite-chine-inde-asie#audit">audits</a> et <a href="/services-controle-qualite-chine-inde-asie#laboratory_testing">tests en laboratoire</a> réalisés au 2ème trimestre 2010 (+ 38% par rapport à 2009), AsiaInspection a relevé des tendances très instructives.
</p>

<p>
Les chiffres de T2 2010 démontrent que les efforts de la Chine pour améliorer la qualité de ses produits exportés semblent porter leurs fruits. Le succès du Made in China auprès des importateurs ne s’est donc pas démenti et continue de s’intensifier. Ainsi, les inspections réalisées en Chine ont grimpé de +35%, soulignant la croissance économique sans égale de la Chine; le dragon chinois est donc définitivement réveillé et son ascension reprend de plus belle.
</p>

</div>
<div class="head-small">
<h2>Le dragon chinois domine le tigre vietnamien</h2>
</div>
<div>
<div>
<p>
Les récents mouvements sociaux dans les usines du sud de la Chine n’ont pas eu raison de la formidable croissance économique du pays. Prédire que cette petite faiblesse aurait pu profiter aux pays voisins serait tentant, mais les chiffres du contrôle qualité indiquent clairement que pour le moment il n’en est rien. 
La Chine continue de magnétiser tous les investissements directs en Asie et de muscler ainsi sa formidable machine de production, distançant toujours plus ses proches voisins. Ainsi entre 2009 et 2010, le Vietnam semble en perte de vitesse et n’a pas enregistré de progression du nombre d’inspections qui y ont été pratiquées.
</p>
<p>
Quant aux produits contrôlés, les inspections de textile et d’habillement présentent la croissance la plus importante (+72.8%). Une amélioration soutenue de la qualité a permis de gagner la confiance des consommateurs et de renforcer ce secteur en pleine croissance.
</p>
</div>

<div class="head-small">
<h2>Les Etats-Unis doublent une Europe essoufflée par la Grèce</h2>
</div>
<div class="para">
<p>
Au premier trimestre 2010, les commandes d’inspections en provenance des Etats-Unis et de l’Union Européenne étaient au coude à coude avec respectivement +11.2% et +11.3%. Toutefois au deuxième trimestre, les USA bondissent de +46% tandis que l’UE chute de -3.8%, signe que l’économie Européenne a été ébranlée par la crise Grecque.
</p>

