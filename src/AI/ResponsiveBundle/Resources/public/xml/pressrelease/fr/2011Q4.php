<h1>Baromètre d'AsiaInspection pour le T4 de 2011 : La Chine ne remplit pas les critères d'une inspection alimentaire sur deux en 2011</h1>
</div>
<div class="para">
<p>
Shenzhen, Chine, 25 Janvier 2012 – AsiaInspection, chef de file dans le domaine des services de contrôle qualité pour les entreprises importatrices de produits asiatiques, a annoncé aujourd'hui le baromètre d'AsiaInspection pour le quatrième trimestre de 2011, un synopsis trimestriel des fabricants et services de contrôle qualité basés en Asie. .
</p>
</div>
<div class="head-small">
<h2>Le taux d'échec des produits alimentaires chinois à l'inspection révèle un réel danger</h2>
</div>
<div class="para">
<p>
Les chiffres d'AsiaInspection en 2011 révèlent 51 % d'échec des produits de Chine continentale aux inspections alimentaires. Si la plupart des produits ont échoué à ces inspections en raison de défauts mineurs, 10 % présentaient des défauts graves, dont un cas extrême de contamination par de grandes quantités de fèces de rongeurs. Ce taux d'échec aux inspections de la moitié des produits alimentaires chinois est d'autant plus alarmant si on le compare au taux moyen d'échec des produits non alimentaires, qui se situe aux environs de 30 %.


</p>
<p>
Ces chiffres déplorables en matière de sécurité alimentaire sont confirmés par le gouvernement. L'administration d'État chinoise pour l'industrie et le commerce fait état de 62 000 cas d'aliments illégaux en 2011. En outre, les autorités ont ordonné la fermeture de 43 000 usines alimentaires dépourvues de licences qui opéraient illégalement et ont retiré leur licence à 576 producteurs au cours de la même période.


</p>
</div>
<div id="block">
<div id="block-para">
<p>
La sécurité alimentaire ne s'arrête pas aux aliments eux-mêmes. Le conditionnement alimentaire, dont le taux d'échec à l'inspection 2011 était de 57 %, est tout aussi important. « Les défauts du conditionnement alimentaire peuvent ne pas sembler essentiels », a confié Sébastien Breteau, PDG d'AsiaInspection. « Mais lorsque la nourriture atteint les rayons des magasins après sa sortie de l'usine, des quantités toxiques d'agents contaminants tels que le formaldéhyde et le plomb peuvent s'être échappées des emballages, ce qui peut contaminer les aliments et mettre les consommateurs en grand danger. » 

</p>
<p>
Selon la FERA, agence gouvernementale pour l'alimentation et l'environnement du Royaume-Uni, des agents contaminants chimiques courants tels que la tristement célèbre mélamine proviennent souvent des boîtes de conserve et des couvercles alimentaires ou de contenants alimentaires en plastique. La mélamine, à l'origine du scandale notoire du lait Sanlu et responsable de 300 000 cas d'empoisonnement, a été découverte en de fortes proportions dans des cargaisons d'aliments pour chien en conserve pas plus tard qu'en janvier 2011.
</p>
