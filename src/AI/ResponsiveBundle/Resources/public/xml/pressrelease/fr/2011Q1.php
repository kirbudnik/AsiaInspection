<h1>Le Baromètre AsiaInspection T1 2011 indique que le secteur manufacturier chinois se déplace vers l’intérieur pour maintenir sa compétitivité à l’exportation</h1>
</div>
<div class="para">
<p>
Shenzhen, China, April 7, 2011 – AsiaInspection, un important prestataire de services de contrôle qualité pour les entreprises important des produits fabriqués en Asie, annonce aujourd’hui la publication du Baromètre AsiaInspection T1 2011, un sommaire trimestriel des statistiques concernant la fabrication et les services de contrôle qualité basés en Asie.


</p>
</div>
<div class="head-small">
<h2>Le secteur manufacturier se déplace vers l’intérieur de la Chine</h2>
</div>
<div id="block">
<div id="block-para">
<p>
Les chiffres des services AsiaInspection indiquent des augmentations significatives des inspections effectuées dans les provinces rurales chinoises, ce qui dénote une évolution des opérations de fabrication en Chine de l'Ouest et du Nord. Entre le T3 2010 et le T1 2011, les inspections dans la province d'Anhui ont augmenté de +175 % et de +118 % dans la province du Hebei. De nombreux travailleurs d'usine, originaires des régions intérieures, restent à la maison après le Nouvel An chinois pour occuper de nouveaux emplois près de chez eux. Ces travailleurs préfèrent les salaires plus bas (dont la moyenne est de 114 USD) des provinces intérieures où le coût de vie est moindre aux salaires plus élevés (dont la moyenne est de 205 USD) des villes du littoral où règne une inflation galopante. 
</p>
<p>
Ces nouveaux emplois sont offerts par les entreprises qui se déplacent vers l'intérieur pour profiter de ces coûts salariaux plus faibles. Cet automne, le fabricant d’iPhone Foxconn achèvera la construction d'une nouvelle usine dans la province intérieure du Henan. Selon les chiffres d’AsiaInspection, les inspections effectuées dans le Henan entre juin 2010 et janvier 2011 ont augmenté de +78 %. Parmi les autres entreprises du palmarès Fortune 500 à déménager vers l'intérieur, on trouve HP à Chongqing et Intel à Chengdu. D’autres sociétés devraient rapidement les imiter (Financial Times).
</p>
</div>
