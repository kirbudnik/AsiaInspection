<p>
« Les villes côtières chinoises ont, ces dernières années, commencé à s'améliorer en tant que fournisseurs fiables. Leurs productions respectent à présent des normes de qualité internationale », a déclaré Sébastien Breteau, PDG d'AsiaInspection. « Les secteurs manufacturiers des provinces rurales de Chine intérieure ne sont pas encore arrivés à maturité et ne maintiennent pas le même niveau de qualité que les régions côtières développées. Il est essentiel que les entreprises sous-traitant leur fabrication vers ces régions s’assurent que chaque produit est conforme aux normes internationales de qualité et de sécurité. »
</p>
</div>
<div class="head-small">
<h2>Les exportations chinoises vers l’Iran continuent à augmenter à cause des sanctions américaines</h2>
</div>
<div class="para">
<p>
Malgré l'agitation continue que connaît la région, le Moyen-Orient est en hausse de +40 % en glissement annuel. Dans la région, l'Iran a affiché la plus forte augmentation, une hausse de +215 %. En raison des sanctions commerciales accrues imposées à l'Iran par les États-Unis et d'autres pays occidentaux, le commerce Chine-Iran a augmenté au cours de la dernière décennie, passant de 2,5 milliards USD en 2000 à 29 milliards USD en 2010, ce nombre devrait atteindre 50 milliards USD d'ici 2015. Tandis que les alliés des États-Unis continuent à sortir de l'Iran, la Chine accroît sa présence dans le pays, en y développant des gisements de pétrole, en important des minéraux et en exportant 11,1 milliards USD de marchandises, dont, notamment, des équipements industriels et des matériaux de construction.
</p>
<p>
Les chiffres des inspections par catégories pour la même période reflètent cette tendance, avec la construction et les éléments mécaniques progressant de +30 %, en hausse pour la première fois depuis 2009, et les accessoires maison et jardin de +43 %.
</p>
</div>
<div class="head-small">
<h2>Par suite de l’embellie économique, les États-Unis et le Canada commandent plus d’inspections</h2>
</div>
<div class="para">
<p>
Pour les États-Unis, les chiffres des commandes de services AsiaInspection ont augmenté de +34 % sur douze ans, et de jusqu'à + 46 % pour le Canada par suite de l’embellie économique nord-américaine. Les commandes de services ont augmenté de +23 % au Royaume-Uni et de +17 % en Allemagne, mais les difficultés économiques ont entraîné des diminutions de -27 % en Espagne et de -41 % en Grèce, ce qui a entrainé une baisse de -8 % pour l’ensemble de l’UE. Les pays émergents ont maintenu la plus forte croissance avec une progression de +66 % pour l’Inde, de +107 % pour le Bangladesh et de +123 % pour le Vietnam.
</p>
