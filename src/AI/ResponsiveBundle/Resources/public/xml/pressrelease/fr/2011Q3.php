<h1>Baromètre AsiaInspection T3 2011 : Avec la hausse des coûts, la Chine sera-t-elle capable de garder son avantage concurrentiel ?</h1>
</div>
<div class="para">
<p>
Shenzhen, China, 10 Octobre, 2011 – AsiaInspection, annonce aujourd'hui le Baromètre AsiaInspection T3 2011, une analyse trimestrielle des statistiques concernant les produits manufacturés et les services de contrôle qualité en Asie.
</p>
</div>
<div class="head-small">
<h2>L'économie chinoise en pleine maturation défie les fabricants</h2>
</div>
<div class="para">
<p>
Les fabricants chinois sont actuellement confrontés à de nombreux défis pour rester compétitifs. On retrouve en tête de liste l'inflation du Yuan, qui a connu 6,2 % de hausse depuis septembre l'an dernier, alors traduit par une hausse du coût des marchandises pour les acheteurs étrangers. Cette inflation a en outre déclenché une augmentation programmée par le gouvernement de 13 % par an du salaire minimum, sur les cinq prochaines années (source : Business Spectator), d'où un coût plus élevé par unité pour les produits chinois. Cette inflation a déjà poussé certains acheteurs vers des pays comme le Bangladesh et l'Inde. Dans ce sens, AsiaInspection a vu une augmentation de 72 % des inspections commandées au Bangladesh et en Inde au troisième trimestre 2011 par rapport à la même période l'an dernier.
</p>
</div>
<div id="block">
<div class="head-small">
<h2>Malgré les défis existants, les chiffres reflètent une demande accrue en produits chinois</h2>
</div>
<div id="block-para">
<p>
Cependant, les chiffres provenant du secteur manufacturier chinois au troisième trimestre, présentent une demande pour ses produits en constante hausse. Le Purchasing Managers Index chinois, bien que légèrement en baisse en septembre à 49,4 contre 50,9 en août, suggère malgré tout une croissance annuelle de 12-13 % d'ici la fin de l'année. Les chiffres d'AsiaInspection montrent également une augmentation de 27,5 % des inspections commandées à l'intérieur de la Chine par rapport à l'an dernier, pour le troisième trimestre 2011.
</p>
<div class="head-small">
<h2>La supériorité de la chaîne logistique chinoise</h2>
</div>
<p>
Pour expliquer ce paradoxe, il nécessaire de considérer que partir de Chine et réorienter sa chaîne d'approvisionnement vers un autre pays d'Asie est une décision difficile, et que les acheteurs sont aussi sensibles au délai d'approvisionnement et à la qualité des produits qu'aux coûts directs.
</p>
