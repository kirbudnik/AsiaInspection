<p>
Pour expliquer ce paradoxe, il nécessaire de considérer que partir de Chine et réorienter sa chaîne d'approvisionnement vers un autre pays d'Asie est une décision difficile, et que les acheteurs sont aussi sensibles au délai d'approvisionnement et à la qualité des produits qu'aux coûts directs.
</p>
<p>
Dans ces deux domaines, la Chine demeure loin devant ses voisins régionaux comme le Vietnam et le Bangladesh, de par la supériorité des infrastructures de sa chaîne logistique établie sur les 30 dernières années. Pour illustrer cet écart structurel, rappelons que la Chine exploite 8 des 20 ports à conteneurs les plus importants du monde (9 en comptant le port de Kaohsiung à Taïwan), tandis que tous les autres pays d'Asie du Sud-est combinés en exploitent 3. La Chine dispose d'autre part d'une force ouvrière considérablement plus conséquente, comptant 227 millions d'individus contre 67 millions pour l'Inde et 22 millions pour le Bangladesh. En ce qui concerne la qualité et malgré les défis que doit encore affronter le « Made in China », un chiffre intéressant d'AsiaInspection montre qu’une inspection a 23 % de chances de plus d'échouer au Bangladesh et 16 % en Inde qu'en Chine.
</p>
</div>
<div class="head-small">
<h2>Les industries de main-d'oeuvre sont les plus touchées</h2>
</div>
<div class="para">
<p>
Le poids de la main d’oeuvre varie considérablement en fonction du secteur ainsi toutes les industries chinoises n'ont pas été touchées de la même façon par les hausses des coûts. Pour illustrer ce point, AsiaInspection a connu une baisse de 20 % au troisième trimestre dans les inspections effectuées en Chine sur les textiles, un secteur traditionnellement utilisant davantage de main-d'oeuvre, tandis que les biens durables, dont la production se prête à l'automatisation, ont subi une hausse de 20 %. Ceci explique la raison pour laquelle les inspections effectuées par AsiaInspection dans les pays dépendants du textile (le Bangladesh et l'Inde) étaient encore en pleine progression au troisième trimestre.
</p>
<p>
Selon le PDG d'AsiaInspection, Sébastien Breteau, « Rien ne dit que pour l'instant l'appréciation continue de sa devise et la hausse des coûts de main-d'oeuvre vont bientôt exclure la Chine de la compétition, ni même qu'elle passera en seconde position. Les coûts directs ne représentent qu'une partie de l’équation pour un importateur. Les infrastructures en constante amélioration de la Chine sont encore loin devant celles de ses voisins, et avec la mise en place de contrôle qualité, elle reste malgré tout une bien meilleure option pour la plupart des acheteurs internationaux. »
</p>
