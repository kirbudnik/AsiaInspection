<div id="head">
	<h1>Baromètre AsiaInspection du troisième trimestre 2012: La croissance Chinoise ralentit? Pas si sûr...</h1>
</div>
<div class="para">
	<p>
		Shenzhen, Chine, le 10 octobre 2012 - AsiaInspection, chef de file dans le domaine du contrôle qualité pour les entreprises importatrices de produits asiatiques, a annoncé aujourd'hui la publication de son baromètre pour le troisième trimestre 2012. Le baromètre trimestriel AsiaInspection propose un synopsis d’indicateurs industriels et commerciaux liés à l’activité du contrôle qualité en Asie.
	</p>
</div>
<div class="head-small">
	<h2>Malgré les inquiétudes sur le PIB, les données du contrôle qualité montrent une croissance forte en Chine</h2>
</div>
<div id="block">
	<div id="block-para">
		<p>
			Les chiffres du dernier baromètre AsiaInspection montrent que malgré le récent ralentissement de la croissance observée en Chine depuis trois décennies, l'économie du pays, son secteur industriel et l'industrie du contrôle qualité maintiennent une croissance stable. Les données d'AsiaInspection concernant le T3 indiquent que le volume d'inspections en Chine a progressé de <strong>+23.7% par rapport au trimestre précédent, et de +12.8% sur l'année.</strong>.
		</p>
		<p>
			Alors que la plupart des analystes prédisent le renversement de l'économie chinoise au profit de ses voisins d'Asie du Sud-Est, de tels chiffres de croissance font résonner le point de vue différent de certains médias: non seulement après presque trois décennies de croissance à 2 chiffres un ralentissement de l'économie chinoise est inévitable, et peut-être même souhaitable, mais en plus il est largement exagéré.
		</p>
	</div>
	<div id="picture">
		<img src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/img/$file/electrical-inspection.JPG">
		<p>"Une inspection d'appareils électriques en Chine"</p>
	</div>
	<div style="clear:both;"></div>
</div>
<div class="para">
	<p>
		Le taux de croissance de la Chine en pourcentage est certes moins élevé que les années précédentes (une évolution attendue dans une économie en voie de maturation, et inévitable avec la loi des rendements décroissants). Mais de 2010 à 2011, le PIB chinois est passé de 6.36 trillions à 7.32 trillions de dollars, soit une <strong>augmentation équivalente à la totalité du PIB du Mexique en 2011</strong>. Justin Yifu Lin, ancien chef économiste de la Banque Mondiale, prédit ainsi que l'économie chinoise continuerait à croître à un rythme élevé pendant encore "20 ou 30 ans".
	</p>
	<p>
"Il manque au Bangladesh, au Viêt Nam ou même à l'Inde deux éléments qui assurent à la Chine son statut d'usine du monde: 1,4 milliards d'ouvriers et des infrastructures de qualité", estime Sébastien Breateau, Président d'AsiaInspection. "Chez AsiaInspection, nous pensons que la Chine n'est pas prête de céder son statut de leader mondial dans la production manufacturière. Quand on garde à l'esprit les proportions dans les chiffres avancés, on se rend compte qu'en fait de ralentissement on assiste surtout à des petits creux au sein d'une tendance de croissance à long terme."
	</p>
	<p>
Outre les inquiétudes concernant le niveau de la production manufacturière de la Chine, de nombreux analystes insistent sur la baisse de la demande des pays étrangers comme étant une des causes principales du ralentissement de l'économie chinoise. Cependant les données d'AsiaInspection concernant le T3 montrent que malgré une situation économique morose en Amérique du Nord et en Europe , le nombre des inspections a progressé de <strong>+9%</strong> en Europe et de <strong>+8%</strong> pour tous les pays du G8 confondus par rapport à l'année dernière. Les régions avec les plus forts taux de croissance annuel sont l'Amérique du Sud <strong>(+66%)</strong> et l'Afrique <strong>(+39%)</strong>.
	</p>
</div>
<div class="head-small">
	<h2>En Chine, la croissance continue, tout comme les problèmes qualité</h2>
</div>
<div class="para">
	<p>
	Bien que la Chine tend à devenir fournisseur de produits à valeur ajoutée, la demande de contrôle qualité n'a pas pour autant diminué. Des scandales éclatent encore avec par exemple la FDA (Food and Drug Administration) aux Etats-Unis qui enquête sur des friandises pour animaux contaminées ayant entraîné des problèmes de santé pour plus de 2200 animaux de compagnie et même la mort pour 320 d'entre eux. Les informations d'AsiaInspection montrent ainsi sur le T3 2012 que les inspections de produits alimentaires en Chine ont augmenté de <strong>+66%</strong> par rapport à l'année dernière.
	</p>
	<p>
	De même, on trouve dans les rappels de produits pour enfant "Made-in-China" aux Etats-Unis et au Canada plus de 2 millions d'accessoires de bain pour bébé, à cause de risques de sécurité ayant entraîné des blessures à la tête pour sept enfants. AsiaInspection a vu au troisième trimestre de 2012 la demande pour les inspections de jouets augmenter de +23% par rapport à l'année dernière. En parallèle de cette augmentation continue des rappels de produits, les chiffres d'AsiaInspection montrent que la demande pour les tests de sécurité en laboratoire a augmenté de <strong>+60%</strong> par rapport à 2011 pour la période allant de janvier à septembre.
	</p>
