<p style="text-align:justify;">2013年對於全球的品牌商和零售業來說是面對挑戰的一年。以拉納廣場（Rana Plaza）事件為例，他們面對了越來越多的供應鏈社會問題。拉納市場事件是指今年5月孟加拉一家工廠倒塌奪走1,129條生命的悲剧。</p>
<p style="text-align:justify;">這一年罷工和暴動遍布印度、孟加拉、柬埔寨、緬甸的工廠，工人紛紛要求提高工資。在孟加拉，工人的工資是中國工人的一半；目前柬埔寨是每月80美元/月，將在2014年4月增加到95美元/月。</p>
<p style="text-align:justify;">在美國勞工部刊出關於亞太地區7800萬童工<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">1</sup></a>的報告<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">2</sup></a>之後，童工問題再次被放到聚光燈下。</p>
<p style="text-align:justify;">AsiaInspection的數據反映出各地的品牌商和零售商正在采取行動，與2012年相比，2013年AsiaInspection亞洲地區的審核訂單比去年增長了<b>+61%</b>。孟加拉仍然在聚光燈下，但買家在所有采購地域紛紛采用審核的方式確保各自供應鏈之安全。較去年同期，孟加拉增長<b>47%</b>、中國增長58％，印度的增長高達<b>112%</b>。</p>
<p style="text-align:justify;">由於在孟加拉的悲劇事件及其以服裝制造行業為主導的狀況，消息傳出買家開始尋求在該地區的替代。AsiaInspection 驗貨訂單量的數據也支持了這一說法。孟加拉作為僅次於中國的服裝最大出口國，驗貨訂單比往年增長了<b>+36%</b>，而印度超過了這個數字達到<b>+44%</b>，越南 <b>+71%</b>，柬埔寨增長量高達<b>+114%</b>。</p>
