<h2 style="font-size:18px;">聖誕季剛過 中國制造的玩具安全了嗎？</h2>
<p>中國繼續主導玩具制造行業，在西方國家出售的逾<b>80%</b>的玩具是中國制造。聖誕季剛過去，消費者的安全被提上最後一個季度的日程。聖誕季的玩具銷售高達所有銷售活動的<b>70%</b>。</p>
<p>11月，200,000中國制造的玩具在美國港口因含有含有高濃度的鄰苯二甲酸鹽（在塑料中使用違禁化學品）而被查獲。同樣的案例也發生在法國，12月份有30,000件不達標的中國玩具被海關銷毀。與此同時，美國公共利益研究集團對50種玩具進行分析發現，鉛含量超出安全限制29倍；禁止使用的鄰苯二甲酸鹽被運用，鎘超標以及玩具具有窒息的風險。</p>
<p>AsiaInspection在玩具產業的數據顯示了令人擔憂的問題。2013年在亞洲玩具檢驗的不合格率從2012年的<b>27%</b>上升到<b>33%</b>。大多數不合格的驗貨問題屬於小的並不危及生命，但也有超過<b>4%</b>的在AI的玩具檢驗沒有通過相應的安全標準。</p>

<h2 style="font-size:18px;">采購力：中國 vs 世界其他國家</h2>
<p>盡管市場擔心中國的競爭力會受人民幣升值及工資上漲的影響而下降，中國的出口量屢創新高。11月同比增長12.7％，1月至11月較2012年增長<b>9.3%</b>。這使中國達到繼2008年以來最大的年度貿易順差。</p>
<p>AsiaInspection驗貨數據也印證了這點。2013年AsiaInspection在中國的驗貨訂單量較2012年上漲了<b>17%</b>。中國仍是AI客戶的最大的采購區域，而孟加拉出口總額僅為中國的<b>1%</b>。</p>
<p>從百分比增長來看，2013年采購區域有新的變化趨勢。非洲今年下半年出口量增加了<b>47%</b>，越南增長<b>71%</b>。盡管有勞工對工資抗議，柬埔寨的數據仍翻倍。</p>

<h2 style="font-size:18px;">展望2014年全球采購發展趨勢</h2>
<p>2013年的供應鏈管理問題，不僅引起采購相關人員高度重視，更引起了消費者的關註。拉納廣場（Rana Plaza）事件悲劇，歐洲馬肉醜聞以及各食品安全問題的案例在中國不斷重演使得全球食品安全意識提高並且加強了對更嚴格的端到端的全球供應鏈管理的需求。</p>
<p>“西方采購分公司不了解他們產品的產地以及外判工廠狀況的現象頻頻出現，這種情況註定了失敗”，AsiaInspection的首席執行官Sebastien Breteau說，“知名品牌H&M, 蘋果和惠普已經公開部分乃至完整的合格供應商。我們將會看到更多的以知名企業為首的此類舉措，對供應商采取完全透明化控制。預計2014年全球供應鏈中的透明化控制將成為現實。”</p>

