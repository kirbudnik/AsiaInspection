<h1>AsiaInspection第四季度報表：全球供應鏈具有社會責任的一年？</h1>
<p><b>2014年1月14日</b> - 中國深圳-AsiaInspection今日發布2013年第四季度報表，發表年度性的外判制造業以及質量控制服務行業評論。AsiaInspection是領先的質量控制服務供應商，我們與國際品牌、零售商及進口商建立合作關系，幫助他們管理在亞洲、非洲、南歐及拉丁美洲的供應鏈並保障其其供應鏈安全。</p>

<h2 style="font-size:18px;">拉納大廈（Rana Plaza）事件之後的亞洲制造業</h2>
