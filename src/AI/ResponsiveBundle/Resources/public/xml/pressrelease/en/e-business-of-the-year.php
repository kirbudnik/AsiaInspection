<p>Early September, AsiaInspection was extremely pleased to be rewarded for its successful online platform with the "E-Business of the Year" award from Alibaba.com, the leading online trade platform for global importers.</p>
<p>Out of 1,200 initial candidates, AsiaInspection was eventually selected as one of the 10 winners for the Hong Kong region, and attended the Alifest event in Hangzhou on September,15th.</p>
<p>AsiaInspection's online platform now serves more than 1,200 Importers in 58 different countries.</p>
<p>Until 1995, Jack Ma had never touched a computer. Today he is China's best known Internet entrepreneur. In 1995 he founded China Pages, the first web-based company in China. Four years later, he launched Alibaba.com , which counts more than 3 million members today. Under Jack's leadership, the Alibaba Group has become a global B2B website and now operates businesses with leading positions in e-marketplaces (Alibaba.com and Taobao) and online payment solution (Alipay).</p>

