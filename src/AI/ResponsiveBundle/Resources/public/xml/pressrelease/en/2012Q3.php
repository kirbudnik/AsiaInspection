	</p>
</div>
<div class="head-small">
	<h2>Inspection Data in China Shows Strong Growth Despite GDP Concerns</h2>
</div>
<div id="block">
	<div id="block-para">
		<p>
			AsiaInspection Q3 figures indicate that despite a recent slowdown in the unprecedented growth seen from China over the last three decades,
			the country's economy, manufacturing sector and <a href="/">quality control</a> industry are sustaining stable growth. AsiaInspection Q3 data shows inspections
			performed in China were up <strong>+23.7% quarter-over-quarter and +12.8% year-over-year</strong>.
		</p>
		<p>
			While most analysts are predicting a downturn in the Chinese economy to the benefit of Southeast Asian neighbors,
			this kind of continued growth echoes the perspective some dissenting media are trying to provide: That after three
			decades of unprecedented double-digit growth, a slowdown in China's economy is not only inevitable, and perhaps even desirable,
			it's been blown out of proportion.
		</p>
	</div>
	<div id="picture">
		<img alt="Electrical Product Inspection in China | AsiaInspection" src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/img/$file/electrical-inspection.JPG">
		<p>"An electrical product inspection in China"</p>
	</div>
	<div style="clear:both;"></div>
</div>
<div class="para">
	<p>
		China's economic growth in terms of percentage is certainly lower than in previous years,
		a normal progression with maturing economies and inevitable with the law of diminishing returns.
		From 2010 to 2011, China's year total GDP nevertheless increased from 6.36 trillion to 7.32 trillion USD,
		<strong>adding roughly the equivalent of the entire 2011 GDP of Mexico to its total</strong>. Justin Yifu Lin,
		former chief economist at the World Bank, predicted that the Chinese economy would continue its high growth rate for "another 20 to 30 years".
	</p>
	<p>
		"Bangladesh, Vietnam and even India lack the two components that secure China's position as the World's manufacturer:
		1.4 billion workers and an advanced infrastructure," said AsiaInspection CEO Sebastien Breteau. "At AsiaInspection,
		we don't feel that China will be relinquishing its dominant position in manufacturing any time soon. Bearing proportions in
		mind, any slowdown should be viewed as a dip in a much larger upward trend."
	</p>
	<p>
		In addition to concerns about China's manufacturing output level, many analysts point to shrinking foreign demand as a key cause of China's slump.
		By contrast AsiaInspection Q3 2012 data shows that despite the bleak North America and European economies,
		Y/Y ordered inspection figures for Europe were up <strong>+9%</strong> and the combined G8 countries <strong>+8%</strong>.
		The regions that showed the strongest Y/Y growth were South America, up <strong>+66%</strong> and Africa <strong>+39%</strong>.
	</p>
</div>
<div class="head-small">
	<h2>As Remarkable Growth Continues, China's Quality Issues Persist</h2>
</div>
<div class="para">
	<p>
		Though China is moving into the role of a value-added supplier, demand for <a href="/quality-control-services">quality control</a> has not dropped.
		Product scandals continue with the United States Food and Drug Administration investigating contaminated jerky treats from China linked to 2,200
		pet illnesses and 320 deaths. AsiaInspection data correspondingly shows Q3 2012 demand for <a href="http://www.asiafoodinspection.com" target="_blank">food inspections</a> up <strong>+66%</strong> Y/Y.
	</p>
	<p>
		Similarly, recalled Made-in-China children's products include over 2 million infant bath accessories in the U.S. and Canada due to a hazard that
		has resulted in seven infant head injuries. AsiaInspection saw Q3 2012 demand for <a href="/testing/toy-safety-and-lab-testing">toy inspections</a> increase <strong>+23%</strong> Y/Y.
		Amidst this continued surge in recalls, AsiaInspection figures show demand for <a href="/quality-control-services/laboratory-testing">laboratory testing</a> increase <strong>+60%</strong>
		year-over-year for the period of January through September.
	</p>
