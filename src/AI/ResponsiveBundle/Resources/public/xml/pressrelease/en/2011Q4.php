<div class="para">
<p>
Shenzhen, China, January 25, 2012- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
today announces the AsiaInspection Q4 2011 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
</p>
</div>
<div class="head-small">
<h2>Inspection Failure Rates Show China Food Danger Critical</h2>
</div>
<div class="para">
<p>
AsiaInspection figures show in 2011 51% of food inspections conducted in Mainland China failed. While the majority of these inspections were failed because of minor defects, 10% were for critical defects with an extreme case involving contamination by a large quantity of rodent fecal matter. The fact that over half of all Chinese food inspections fail is even more alarming when compared to an average failure rate for non-food products of about 30%.
</p>
<p>
Evidence of this overwhelming deficit in food safety is supported by government figures. According to China's State Administration for Industry and Commerce, in 2011 62,000 illegal food cases were reported. Additionally, authorities stopped the operation of 43,000 unlicensed food-producing businesses found to be operating illegally and revoked the business licenses of 576 operators during the same period.
</p>
</div>
<div id="block">
<div id="block-para">
<p>
Food safety is not limited to only the food itself. Food packaging, with a 2011 inspection failure rate of 57% is just as critical.
"Food packaging defects may not seem critical", says Sebastien Breteau, CEO of AsiaInspection. "But by the time food leaves the factory and hits store shelves,
toxic amounts of contaminants like formaldehyde and lead can leech out of packaging, contaminate food and cause serious harm to consumers."
</p>
<p>
According to FERA, the United Kingdom's governing food and environment agency, common chemical contaminants like the infamous melamine often come from food cans and lids or from plastic food containers. Melamine - responsible for the infamous 2008 Sanlu milk scandal sickening over 300,000 -
has been found at high levels in canned dog food shipments as recently as January 2011.
</p>
