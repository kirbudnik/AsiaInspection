<p style="font-family: Arial,sans-serif; font-size: 14px;">Underpinning the efforts of consumers, there are various laws adding the necessary legal weight such as the introduction earlier this year of the UK’s Modern Slavery Act which commits companies above a certain size to examine their entire supply chain for breaches.</p>

<p style="font-family: Arial,sans-serif; font-size: 14px;">“Most people think slavery ended over a hundred years ago – in reality, it only became illegal globally in 2007 and worse still, it continues unabated. It is rampant in key developing markets such as China, Pakistan and India – countries that are also the world’s production powerhouses. We believe that as consumer awareness grows, the pressure on companies and governments to take action will ensure we start to see the end of this dark stain of the 21st Century,” concludes Breteau.  </p>

