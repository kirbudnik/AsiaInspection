<p><b>Hong Kong, September 30, 2015</b> - AsiaInspection, a global testing, auditing and inspection company, today released its latest whitepaper on modern day slavery, a highly disturbing reality that is woven into nearly every supply chain around the world.</p>


<p style="font-family: Arial,sans-serif; font-size: 14px;">The scale of the problem is estimated at 27 million slaves globally, the vast majority of whom are involved in apparel and textiles manufacturing, fishing, mining and construction.</p>

<p style="font-family: Arial,sans-serif; font-size: 14px;">“Needless to say, this means that the vast majority of global consumers are unwittingly benefiting from slave labor in one way or another namely through cheaply priced goods” says Sebastien Breteau, CEO of AI.</p> 
<p style="font-family: Arial,sans-serif; font-size: 14px;">With the recent news of the release of 2,000 slave laborers in Thailand’s fishing industry, <a href="/whitepaper/Modern-Slavery">our whitepaper</a> discusses how the world is slowly learning the true extent of the problem and how consumers are driving a sea change by demanding full transparency of supply chains.</p>

<p style="font-family: Arial,sans-serif; font-size: 14px;">“Consumer awareness has begun to increase; cases such as those against Nestle are being brought about by concerned consumers who do not want to support slave labor regardless of how far down the supply chain it is found,” says Breteau.</p>

