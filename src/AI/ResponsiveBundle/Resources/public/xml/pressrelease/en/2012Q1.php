<h1>AsiaInspection Q1 Barometer: Latin America, the Next "Gold Rush" of Chinese Exports.</h1>
</div>
<div class="para">
<p>
Shenzhen, China, April 18, 2012- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
today announces the AsiaInspection Q1 2012 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
</p>
</div>
<div class="head-small">
<h2>Increased Demand for Quality Control from Latin American Importers </h2>
</div>
<div id="block">
<div id="block-para">
<p>
During Q1 2012, inspections in Asia ordered by Latin American importers increased from Q1 2011 by an average of 120%;
notably Colombia 180%, Panama 159%, Chile 87% and Bolivia 56%.Brazil, Latin America's largest economy, saw one of the largest
increases with inspection orders up 240% compared to Q1 2011. This growth, consistent with the increasing momentum of Latin America's economies,
also demonstrates a realization by Latin American importers of the necessity of securing the safety and quality of product at its source.
In larger terms, driven by the economic powerhouse of Brazil, Latin America's overall GDP is expected to increase by 4% in both 2012 and 2013 (Source: IMF).
</p>
<p>
Contingent to this growth, Latin American imports have increased by approximately 20% in recent years (Source: The World Bank).
The strong growth in demand for quality control services seen in the younger economies of Latin America sits in stark contrast when compared with order figures from Europe,
which increased by a modest 13% between Q1 2011 and Q1 2012, reflecting the troubled EU economy.
</p>
