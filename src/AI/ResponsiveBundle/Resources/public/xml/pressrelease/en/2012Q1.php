<div class="head-small">
<h2>What Products are Being Imported from China to Latin America?</h2>
</div>
<div class="para">
<p>
In recent years Brazil and Colombia have emerged as China's second-biggest trading partners after the U.S, owing in large part to their abundant natural resources.
While Latin American exports to China consist mainly of raw materials, including copper and petroleum, imports from China consist predominantly of consumer electronics,
textile and toy products.
</p>
<p>
AsiaInspection Q1 2012 order figures reflect this consumer goods-based trend in Brazil and Colombia.From Q1 2011, inspections of house and gardenware products increased 160%
and textile, apparel and footwear by 270%. Other product categories that saw record growth included toys, which jumped 550% and constructions items, up 294%.
</p>
</div>
<div class="head-small">
<h2>Protectionism: A Potential Barrier to Bilateral Trade</h2>
</div>
<div class="para">
<p>
In order to protect local employment and goods production, some Latin American countries have introduced new stricter customs rates. Additionally,
Argentina, Brazil and Colombia have taken protectionist measures to regulate imports of Chinese products in their territory,
including new import taxes on more than 140 products as of late 2011. (Source: Mercosur Summit, December 20, 21, 2011 - Montevideo)
</p>
<p>
In the boldest move so far, Brazilian Ministry of Finance announced the creation of a customs control campaign aimed specifically at scrutinizing Chinese imports.
These increasing regulations are forcing Latin American importers to implement stricter quality control at the source, to confirm that their Chinese manufactured
products comply with international quality and safety standards and not be rejected by customs and sent back to China.
</p>
<p>
Further substantiating the rush by importers to comply with new regulations at home, Q1 2012 audit orders of Asian factories on behalf of importers from Latin American countries increased by 56% versus Q1 2011.
"Protectionist measures have certainly had an impact on Sino-Latin American trade", said Sebastien Breteau, CEO of AsiaInspection. "
To continue safely growing their business within this new regulatory climate, every Latin American importer must reinforce international quality control standards in their factory.
The fact that we're seeing importers taking an increasing responsibility for their product and business reputation is encouraging."
</p>
