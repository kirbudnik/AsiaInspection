<p><b>Hong Kong, September 22, 2015</b>- Testing the safety of products is critical for all manufacturers – the health and safety of consumers is paramount and strict standards apply. </p>



<p>Finding a supplier that makes the process as straightforward as possible just got easier with today’s announcement that AsiaInspection, a fast-expanding international testing, inspection and auditing company, has acquired Anseco Group, a US-headquartered laboratory company that specializes in testing toys, children’s products and hardlines.</p>

<p>Manufacturers of these products can now benefit from the integration of Anseco’s expert laboratories with AsiaInspection’s market-leading online booking and reporting capabilities. The web-based platform enables retailers and brands to instantly confirm, cancel or view the results of their product tests in real-time from any device and use the data to instantly analyse which factories consistently support their supply chain the best.</p>

