<p>“The ease at which clients can arrange and manage any tests or inspections they require is at the very centre of our business so we are naturally very excited to be bringing the AsiaInspection model to a wider audience” says Sebastien Breteau, CEO of AsiaInspection.</p>

<p>"We share with Anseco the same customer-focused culture. All our clients, both now and in the future, will benefit from the coming together of two innovative, young companies committed to delivering the best.”</p>

<p>The acquisition means that AsiaInspection will now have two additional accredited in-house laboratories under its umbrella, furthering its capabilities to support clients in North America and Asia; the Buffalo, New York lab is an ISO17025 accredited facility and the Hong Kong lab is an HOKLAS accredited facility.</p>

