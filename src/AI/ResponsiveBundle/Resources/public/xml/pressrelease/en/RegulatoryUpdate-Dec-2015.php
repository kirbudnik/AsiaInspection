<h2>New York Counties Stay Bans on Toxic Chemicals in Children’s Products</h2>
<p>Rockland and Albany Counties in New York State have agreed to postpone the enforcement of their Toxic Free Toys Acts (TFTA) until 2016. The stays will give both counties time to develop and implement their regulations in line with existing federal laws.  Albany County has already been challenged with litigation in part due to the fact that their TFTA includes chemical restrictions that pre-empt federal law. Rockland County has agreed to a stay to avoid similar litigation. Both Suffolk and Westchester Counties in New York have similar TFTAs scheduled to go into effect during 2016.</p>
