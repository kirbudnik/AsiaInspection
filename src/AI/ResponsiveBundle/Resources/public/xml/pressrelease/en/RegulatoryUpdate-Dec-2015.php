		<ul>
			<li style="margin-bottom:10px;">The annual summary of settlements consistently shows that attorneys’ fees awards significantly exceed the penalty payments to OEHHA.  The theory behind the attorneys’ fees awards is that there is a public benefit which justifies the award.</li>
			<li style="margin-bottom:10px;">The changes to the regulation require the plaintiff provide evidence that the products were or are above the safe harbor limit and that the reformulation will take these products below the safe harbor level showing a “beneficial use”.</li>
			<li style="margin-bottom:10px;">The section will also be amended to clarify that in addition to attorneys’ fees, claims for investigation costs need to be justified with records of time spent.</li>
		</ul>
	</li>
	<li style="margin-bottom:10px;">
		Reduction in Civil Penalties
		<ul>
			<li style="margin-bottom:10px;">The current regulation provides that a settlement with a zero or minimal civil penalty may be appropriate.</li>
			<li style="margin-bottom:10px;">The proposed changes state that the appropriateness of a minimal penalty is dependent on the facts of the case, require greater proof by the plaintiff that proposed actions instead of civil penalties have a connection to the case, and that the settlement is in the public interest.</li>
		</ul>
	</li>
	<li style="margin-bottom:10px;">
		Alternative Settlement Payments
		<ul>
			<li style="margin-bottom:10px;">The proposed changes prohibit the use of any alternative settlement payments that have not been reviewed by the court.</li>
		</ul>
	</li>
	<li style="margin-bottom:10px;">
		Reporting Requirements
		<ul>
			<li style="margin-bottom:10px;">The proposed changes require that any settlements that occur after a 60-day notice are sent, but prior to the filing of a complaint, are reported to the Attorney General’s (AG) office.  These settlements will be subject to the same standards as a case that has been filed. </li>
			<li style="margin-bottom:10px;">Plaintiffs filing complaints or reporting settlements (including out of court) will be required to provide copies to AG Office within 5 days of filing.</li>
		</ul>
	</li>
</ul>

