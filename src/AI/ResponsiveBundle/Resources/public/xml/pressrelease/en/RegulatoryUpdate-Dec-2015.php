<h2>Ban on Customized Pacifiers, Baby Bottles and Nipples for Baby Bottles</h2>
<p>On October 14, 2015, Brazil's National Institute of Metrology, Quality and Technology (INMETRO) issued Administrative Rule No. 517, 2015, which modifies the conditions regulating the marketing of pacifiers, baby bottles and nipples.  </p>
<p>The customisation of these products is now defined as changing the product features from the original certified product, through the adhesion of small parts, such as crystals, pearls, and beads that are generally fixed by glue or adhesives; painting of decorative elements; and alteration of the colour of the product.</p>
<p>Considering the existence of these customisation practices and the potential choking hazards arising from aspiration or ingestion of small parts added to products through subsequent modification, INMETRO prohibits the manufacture, importation, distribution and marketing of these customised children's products in the territory of Brazil. </p>
<p>The rule entered into force on 15 October 2015.</p>

