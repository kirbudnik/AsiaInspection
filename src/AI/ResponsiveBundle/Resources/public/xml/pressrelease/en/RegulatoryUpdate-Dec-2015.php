<h2>CPSC Proposes Direct Final Rule to Clarify Component Part Testing and Lead in Textiles - Delay of Effective Date and Extension of Comment Period</h2>
<p>The Consumer Product Safety Commission (CPSC) proposed a direct final rule to clarify when component part testing can be used and which textile products have been determined not to exceed the allowable lead content limits under the Consumer Product Safety Improvement Act (CPSIA). </p>
<p>The effective date for the direct final rule published October 14, 2015 at 80 FR 61729 is delayed from December 14, 2015, until January 13, 2016 and the comment period is extended to December 14, 2015. The rule will be effective unless the CPSC receives significant adverse comments after which time they would publish notification in the Federal Register withdrawing this direct final rule before its effective date. </p>
<p>Summary of Amendments:</p>
<u>16 CFR 1109, Clarification of the Component Part Rule</u>
<p>Subpart A of 16 CFR 1109 provides the general requirements for component part testing, and subparts B and C provide for additional conditions for specific products and requirements.  The amendment clarifies that component part testing can be used for products or requirements other than those explicitly specified in 16 CFR 1109 Subpart B (lead, heavy metal and phthalates content in paint and substrate) and Subpart C (composite testing). </p>
<p>The amendment also brings two other provisions of the component part rule up to date.</p>
<p>Updates the Toy standard reference to ASTM F963-11 from ASTM F963-08 in section 1109.11 (a).</p>
<p>Section 1109.13 addresses when a certifier may rely on component part testing for phthalates in children's toys and child care articles. The amendment adds a reference to the Commission's guidance concerning inaccessible component parts (16 CFR part 1199). This change will make the provision concerning phthalates (section 1109.13) consistent with the provision concerning lead (section 1109.12) and will help certifiers understand which components are inaccessible and do not need to be tested for phthalate content.</p>
<u>16 CFR 1500.91, Clarification of the Textile Lead Determination</u>
<p>CPSIA Section 101(a) provides that products designed or intended primarily for children ages 12 and younger may not contain more than 100 ppm of lead. A determination by the CPSC in 16 CFR 1500.91 that a material or product does not contain a lead level that exceeds 100 ppm relieves the material or product from the third party testing requirement.</p>
<p>Section 1500.91(d)(7) states that such a determination (exemption) applies to “textiles (excluding after-treatment applications, including screen prints, transfers, decals, or other prints) consisting of [various fibers].” Thus, the rule determined that dyes and dyed textiles do not contain lead. This rule clarifies that “other prints” referred only to those after-treatment applications that use non-dye substances, in which the non-dye substances do not become part of the fiber matrix but remain a surface coating, could contain lead, and are subject to the testing required under the CPSIA for children's products.</p>
