<h2>PROFECO Hosts Third North America Consumer Product Safety Summit</h2>
<p>Mexico’s Consumer Protection Federal Agency (PROFECO) hosted the third North America Consumer Product Safety Summit in Mexico City on November 18 and 19.  PROFECO was joined by the Consumer Product Safety Commission (CPSC) and Health Canada, representing product safety interests in the United States and Canada respectively.  The Summit provides an opportunity for the three national consumer product safety regulators to evaluate their progress under the North America Cooperative Engagement Framework (CEF), a mechanism established during the first North America Summit to enhance collaboration on consumer product safety.</p>
<p>Since the last Summit in 2013, the cooperative efforts of the three government product safety organizations have resulted in:</p>
<ul>
	<li style="margin-bottom:10px;">The establishment of Joint Project Teams to address various concerns including inter-laboratory test methods, customs collaboration and joint outreach campaigns and events.</li>
	<li style="margin-bottom:10px;">Conducting seven trilateral recalls</li>
	<li style="margin-bottom:10px;">Consultation and alignment on product hazards before regulatory and standards development activities</li>
	<li style="margin-bottom:10px;">Increased flow of shared information, particularly in the area of import surveillance</li>
</ul>
<p>During the Summit, several additional areas of focus were identified to further enhance the cooperative efforts among the regulators including:</p>
<ul>
	<li style="margin-bottom:10px;">Further development of cross-border cooperation to prevent trade of hazardous products within North America</li>
	<li style="margin-bottom:10px;">Improved notification regarding hazard reporting when product is distributed in two or more North American countries</li>
	<li style="margin-bottom:10px;">Developing procedures to share information pertaining to e-commerce vendors and suppliers of products that may pose a safety threat to North American consumers</li>
	<li style="margin-bottom:10px;">Developing a trilateral Memorandum of Understanding to sustain and elevate cooperation relative to consumer product safety in North America</li>
</ul>
<p>The regulators from the three jurisdictions will continue to work together to further the objectives under the CEF and have scheduled the next Summit for 2018.</p>

