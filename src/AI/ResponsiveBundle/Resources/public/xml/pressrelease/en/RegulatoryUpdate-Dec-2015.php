<h2>Canadian Products Containing Mercury Regulations (SOR/2014-254) are Implemented</h2>
<p>Products Containing Mercury Regulations (SOR/2014-254) under the Canadian Environmental Protection Act came into force on November 8, 2015.  Any determination of total quantity of mercury made for the purposes of these Regulations must be conducted by a Canadian accrediting body under the International Organization for Standardization standard ISO/IEC 17025:2005 or by a laboratory that is accredited under the Environment Quality Act, R.S.Q., c. Q-2.</p>
<p>The regulations cover all products containing mercury with the exception of:</p>
<ul>
	<li style="margin-bottom:10px;">waste;</li>
	<li style="margin-bottom:10px;">a product that is at the end of its useful life and that is intended to be recycled;</li>
	<li style="margin-bottom:10px;">a food, drug, or cosmetic as defined in section 2 of the Food and Drugs Act;</li>
	<li style="margin-bottom:10px;">a veterinary biologic as defined in subsection 2(1) of the Health of Animals Act;</li>
	<li style="margin-bottom:10px;">a surface coating material as defined in subsection 1(1) of the Surface Coating Materials Regulations or a surface coating material applied to a toy regulated under the Toys Regulations;</li>
	<li style="margin-bottom:10px;">a pest control product as defined in subsection 2(1) of the Pest Control Products Act;</li>
	<li style="margin-bottom:10px;">a feed as defined in section 2 of the Feeds Act;</li>
	<li style="margin-bottom:10px;">a fertilizer as defined in section 2 of the Fertilizers Act;</li>
	<li style="margin-bottom:10px;">an explosive regulated under the Explosives Act;</li>
	<li style="margin-bottom:10px;">ammunition and explosives under the direction or control of the Minister of National Defence;</li>
	<li style="margin-bottom:10px;">a product, other than a battery, that has a mercury concentration of 0.1% or less by weight in homogeneous materials;</li>
	<li style="margin-bottom:10px;">a battery, other than a button cell battery, that has a mercury concentration of 0.0005% or less by weight in homogeneous materials;</li>
	<li style="margin-bottom:10px;">beginning on January 1, 2016, a button cell battery that has a mercury concentration of 0.0005% or less by weight in homogeneous materials;</li>
	<li style="margin-bottom:10px;">from January 1, 2016 until December 31, 2019, a button cell battery that is incorporated into a medical device that is intended to remain in the body for at least 30 consecutive days;</li>
	<li style="margin-bottom:10px;">ores, concentrates and by-products of metallurgic operations; and</li>
	<li style="margin-bottom:10px;">an on-road vehicle as defined in subsection 1(1) of the On-Road Vehicle and Engine Emission Regulations that is of the 2016 model year or of a previous model year as determined under section 5 of those Regulations.</li>
</ul>

<p>Products containing mercury can only be manufactured or imported in Canada if:</p>
<ul>
	<li style="margin-bottom:10px;">
		The product belongs to a product category (listed below) and meets the requirement of maximum total quantity of mercury in the Product:
		<ul>
			<li style="margin-bottom:10px;">Dental amalgam</li>
			<li style="margin-bottom:10px;">Specific lamp for general lighting purposes</li>
			<li style="margin-bottom:10px;">Scientific instruments</li>
			<li style="margin-bottom:10px;">Professional, commercial and industrial photographic film</li>
			<li style="margin-bottom:10px;">Medical device</li>
			<li style="margin-bottom:10px;">Radiation / Infrared light detector</li>
		</ul>
	</li>
	<li style="margin-bottom:10px;">The manufacturer or importer applies for and is issued a permit for the product</li>
</ul>
<p>Permits can be applied for the product if evidence can be provided that there is no technically or economically feasible alternative or substitute. A permit is valid for 3 years after which point it has to be renewed and a label must be attached to the product including information as specified in the regulation.</p>

