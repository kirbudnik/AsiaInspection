<b>OEHHA Proposes Default Levels for Determining Lead and Arsenic in Certain Foods</b>
<p>The California Code of Regulations Title 27, section 25501 addresses that food for human consumption shall not constitute an “exposure” to California Proposition 65 listed chemicals unless the exposure is due to chemicals that are naturally occurring in the food.  Currently the regulation allows a defendant, in determining the amount of a listed Proposition 65 contaminant in a food product, to deduct the amount that is naturally-occurring as a result of absorption or uptake from the soil in which it is grown.</p>
<p>The Office of Environmental Health Hazard Assessment (OEHHA) has proposed default naturally-occurring levels for the following foods:</p>
<ul>
	<li style="margin-bottom:10px;">Inorganic arsenic in white rice grain – 60 parts per billion (ppb)</li>
	<li style="margin-bottom:10px;">Inorganic arsenic in brown rice grain – 130 ppb</li>
	<li style="margin-bottom:10px;">Lead in raw leafy vegetables – 8.8 ppb</li>
	<li style="margin-bottom:10px;">Lead in raw non-leafy vegetables – 6.2 ppb</li>
	<li style="margin-bottom:10px;">Lead in fruit, meat, seafood, eggs, and fresh milk – 6.2 ppb</li>
</ul>
<p>The above amounts would be subtracted from the measured level of the contaminant in the food product for purposes of determining the exposure level of the chemical to the consumer.</p>

