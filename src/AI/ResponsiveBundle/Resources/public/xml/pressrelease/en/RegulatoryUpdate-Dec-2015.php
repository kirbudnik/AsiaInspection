<h2>FDA Issues Draft Guidance on EMC of Electrically-Powered Medical Devices</h2>
<p>On November 2, 2015, the U.S.  Food and Drug Administration (FDA) issued a draft guidance document entitled “Information to Support a Claim of Electromagnetic Compatibility (EMC) of Electrically-Powered Medical Devices.” This document describes the types of information that should be provided to support a claim of electromagnetic compatibility (EMC) in a premarket submission for an electrically powered medical device. For the purpose of this document, EMC is defined as the ability of a device to function (a) properly in its intended electromagnetic environment, including immunity to electromagnetic disturbance (interference), and (b) without introducing excessive electromagnetic disturbances (emissions) that might interfere with other devices.</p>
<p>According to this guidance document, a claim of EMC for a device should be accompanied by the information listed below:</p>
<ul>
	<li style="margin-bottom:10px;">Summary of the testing that was performed to support EMC</li>
	<li style="margin-bottom:10px;">The specifications of the standard that were met (including immunity test levels)</li>
	<li style="margin-bottom:10px;">The device-specific pass/fail criteria used</li>
	<li style="margin-bottom:10px;">The specific functions of the device that were tested (e.g. for IEC 60601-1-2, including essential performance) and how these functions were monitored</li>
	<li style="margin-bottom:10px;">The performance of the device during each test, indicating if the device met the emissions and immunity pass/fail criteria</li>
	<li style="margin-bottom:10px;">An identification of and a justification for any of the standard’s allowances that were used</li>
	<li style="margin-bottom:10px;">A description of and justification for any deviations from the specifications of the referenced standard</li>
	<li style="margin-bottom:10px;">The device labelling and evidence of compliance with the reference standard’s labelling specifications</li>
	<li style="margin-bottom:10px;">A detailed description of all changes or modifications that were made to the device in order to pass any of the EMC tests</li>
</ul>
<p>FDA welcomes comments and suggestions on this draft through mid-December. </p>
<p>This draft guidance is not final nor is it in effect at this time.</p>
