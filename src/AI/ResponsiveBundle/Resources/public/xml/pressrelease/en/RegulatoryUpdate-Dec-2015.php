<p>The Hong Kong Special Administrative Region (HKSAR) Government proposed adopting up-to-date safety standards promulgated by the relevant standards institutions for toys and some children’s products listed in Schedule 2 (“Schedule 2 products”) under the Toys and Children’s Products Safety Ordinance (Cap. 424) (“the Ordinance”). Comments on the proposal were accepted through December 1, 2015.</p>
