	"China's coastal cities have begun to improve themselves in recent years as reliable suppliers that can produce to international quality standards."
	said Sebastien Breteau, CEO of AsiaInspection. "The manufacturing sector of provinces from rural inland China have not yet fully matured,
	nor maintained the same quality levels as developed coastal regions. It's vital that businesses outsourcing manufacturing to those regions ensure every product meets international quality and safety standards."
	</p>
	</div>
	<div class="head-small">
	<h2>Chinese Exports to Iran Continue to Increase Thanks to U.S. Sanctions</h2>
	</div>
	<div class="para">
	<p>
	Despite continued turmoil in the region, the Middle East was up +40% year-over-year. Within the region, Iran saw the largest increase, up +215%.
	Thanks to increasing trade sanctions against Iran by the U.S. and other Western nations, China-Iran trade has increased over the last decade, from $2.5 billion USD in 2000 to $29 billion in 2010;
	this number is expected to increase to $50 billion by 2015.As U.S. allies continue to pull out of Iran, China is increasing its presence in the country, developing oilfields, importing minerals and exporting $11.1 billion worth of goods,
	including industrial machinery and construction materials.
	</p>
	<p>
	Inspection by category figures for the same period reflect this trend, with Construction and Mechanical Items up +30%, increasing for the first time since 2009, and with Home and Gardenware up +43%.
	</p>
	</div>
	<div class="head-small">
	<h2>U.S. and Canada Ordering More Inspections as Economy Improves</h2>
	</div>
	<div class="para">
	<p>
	AsiaInspection ordered service figures for the United States were up +34% year-over-year, with Canada up + 46%, reflecting an improved North American economy.
	Though service orders for the UK were up +23% and Germany +17%, economic woes led to decreases of -27% in Spain and -41% in Greece, which contributed to the EU decreasing -8% overall.
	Emerging countries maintained the highest growth levels with India up +66%, Bangladesh up +107% and Vietnam up +123%.
	</p>
