	<h1>AsiaInspection Q1 2011 Barometer: China's Industry Moving Inland to Keep Exports Competitive</h1>
	</div>
	<div class="para">
	<p>
	Shenzhen, China, April 7, 2011- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
	today announces the AsiaInspection Q1 2011 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
	</p>
	</div>
	<div class="head-small">
	<h2>Manufacturing Moving to China's Interior</h2>
	</div>
	<div id="block">
	<div id="block-para">
	<p>
	AsiaInspection service figures show significant increases in inspections performed in China's rural inland provinces, reflecting a shift of manufacturing operations in China to the west and north.
	From Q3 2010 to Q1 2011, inspections in Anhui Province have increased +175% and +118% in Hebei Province. Many factory workers, originally from inland regions,
	are staying home after Chinese New Year to fill new jobs closer to home, preferring the lower inland province average wages of $114 USD, but with a lower cost-of-living,
	to the higher average coastal city wages of $205 USD, but with rampant inflation.
	</p>
	<p>
	These new jobs are a result of companies moving inland to take advantage of these lower labor costs.
	This fall, iPhone manufacturer Foxconn will complete construction of a new plant in inland Henan province. According to AsiaInspection figures, from June 2010 until January 2011, inspections performed in Henan
	increased +78%. Other Fortune 500 's moving inland include HP to Chongqing and Intel to Chengdu, with more companies expected to follow soon (Financial Times).
	</p>
	</div>
