	<h1>AsiaInspection 2011 Q3 Barometer: With Rising Costs, Can China Keep its Competitive Edge?</h1>
	</div>
	<div class="para">
	<p>
	Shenzhen, China, October 10, 2011- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
	today announces the AsiaInspection Q3 2011 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
	</p>
	</div>
	<div class="head-small">
	<h2>China's Maturing Economy Challenges Manufacturers</h2>
	</div>
	<div class="para">
	<p>
	China's manufacturers currently face many challenges to stay competitive. Chief among these challenges is the inflation of the Yuan, up 6.2% from September last year, thus increasing the cost of goods for foreign buyers.
	In addition, this inflation has sparked a government-planned minimum wage increase of 13% every year for the next five years (source: Business Spectator), meaning a higher per-unit cost for Chinese products across the board.
	This inflation has already driven some international buyers to countries like Bangladesh and India.
	Supporting this, AsiaInspection saw a 72% increase of ordered inspections in Bangladesh and India in Q3 2011 compared to the same period last year.
	</p>
	</div>
	<div id="block">
	<div class="head-small">
	<h2>Despite Challenges, Figures Indicate Increased Demand for China Products</h2>
	</div>
	<div id="block-para">
	<p>
	Even with these factors, figures from China's manufacturing industry in Q3 indicate that demand for Chinese products continues to grow. China's purchasing managers index (PMI), while down slightly in September at 49.4 from 50.9 in August,
	still suggests 12-13% annual growth by year's end. AsiaInspection figures also saw a 27.5% increase in inspections ordered within China year-over-year in Q3 2011.
	</p>
	<div class="head-small">
	<h2>China's Superior Supply Chain </h2>
	</div>
	<p>
	To explain this paradox, it helps to consider that pulling out of China and switching sourcing operations to another Asian country is a tough trade-off,
	and buyers are as sensitive to lead time and product quality as they are to input costs.
	</p>
	</div>
	<div id="picture">
	<img src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/Newsletter-2011-Q3-Barometer/$file/hanghzou-inspection.jpg">
	<p>A textile inspection outside of Hangzhou, China.</p>
	</div>
	<div style="clear:both;"></div>
	</div>
	<div class="para">
	<p>
	In these two areas, China remains far ahead of its regional neighbors like Vietnam and Bangladesh, owing to a superior supply-chain infrastructure that was built over the past 30 years. To picture this structural gap, it helps to remember that China operates 8 of the 20 busiest container ports worldwide (9 if you include Kaohsiung port in Taiwan) - while all other Southeast Asian countries combined only operate 3.
	Additionally, China has a significantly larger industrial labor force of 227 million compared to that of India with 67 million and Bangladesh 22 million.
	</p>
	<p>
	When it comes to quality, and despite challenges remaining for the 'made-in-China' brand, an interesting AsiaInspection figure shows that inspections are 23% more likely to fail inspection in Bangladesh and 16% in India versus China.
	</p>
	</div>
	<div class="head-small">
	<h2>Labor Intensive Industries Most Impacted</h2>
	</div>
	<div class="para">
	<p>
	Manual labor varies greatly by industry, and thus not all industries in China have been equally impacted by cost increases. Reflecting this, AsiaInspection saw a 20% decrease in Q3 in inspections performed in China on textiles, traditionally more labor-intensive, while hardline products, which allow for more automation in production, were up 20%.
	This goes far in explaining why AsiaInspection performed inspections in textile-dependent countries (Bangladesh and India) were still booming in Q3.
	</p>
	<p>
	According to AsiaInspection CEO Sebastien Breteau, "It hasn't yet proven true that because of continuous currency appreciation and rising labor costs, China will soon be out of the sourcing game, or even in second place. Input cost is only a part of the equation for a sourcing professional.
	China's ever improving infrastructure is still far ahead of its neighbors, and with proper quality control in place, it will still remain a better overall option for most international buyers."
	</p>
