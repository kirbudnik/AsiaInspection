<div class="head-small">
<h2>  ما المنتجات  التي يتم استيرادها من الصين  إلى أمريكا اللاتينية؟</h2>
</div>
<div class="para">
<p>
في السنوات الأخيرة برزت البرازيل وكولومبيا كثاني أكبرشريك تجاري للصين بعد الولايات المتحدة، ويرجع ذلك إلى حد كبير إلى مواردها الطبيعية الوفيرة. كما ارتفعت الصادرات إلى أمريكا اللاتينية تتكون أساسا من الصين المواد الخام، بما في ذلك النحاس والبترول وتتكون الواردات من الصين في الغالب من المنتجات الالكترونية والمنسوجات ولعب الأطفال .

</p>
<p>
أرقام الشركة تعكس هذا النظام للسلع الاستهلاكية على أساس الاتجاه في البرازيل وكولومبيا. من الربع الاول، وزيادة عمليات التفتيش من منتجات منزل والمنتجات بنسبه 160٪ و منتجات الغزل والنسيج والملابس والاحذية بنسبة 270٪. وشملت فئات المنتجات الأخرى التي شهدت نموا قياسيا  من منتجات اللعب، والتي قفزت 550٪ و منتجات مواد البناء، بزيادة 294٪.

</p>
</div>
<div class="head-small">
<h2>سياسة الحماية الصناعية: الحواجز المحتملة للتجارة الثنائية</h2>
</div>
<div class="para">
<p>
من أجل حماية العمالة المحلية وإنتاج السلع، وأدخلت بعض بلدان أمريكا اللاتينية معدلات جديدة أكثر صرامة الجمركية. بالإضافة إلى ذلك، اتخذت الأرجنتين والبرازيل وكولومبيا التدابير الحمائية لضبط الواردات من المنتجات الصينية في أراضيها، بما في ذلك الضرائب على الواردات الجديدة على أكثر من 140 منتج وذلك اعتبارا من أواخر عام 2011. (المصدر: ميركوسور القمة، 20 ديسمبر، 21، 2011 - مونتيفيديو)

</p>
<p>
في أجرأ خطوة حتى الآن، أعلنت وزارة المالية البرازيلية إنشاء حملة الرقابة الجمركية تستهدف على وجه التحديد التدقيق الواردات الصينية. هذه اللوائح تجبر المستوردين زيادة في أمريكا اللاتينية لتنفيذ أكثر صرامة لمراقبة الجودة في المصدر، للتأكد من أن منتجاتها المصنعة الصينية يتوافق مع الجودة العالمية ومعايير السلامة وعدم رفض من قبل الجمارك واعادتهم الى الصين.

</p>
<p>
تثبت الاندفاع من قبل المستوردين للامتثال لقواعد جديدة في المنزل، وأوامر التدقيق   من المصانع الآسيوية نيابة عن المستوردين من دول أمريكا اللاتينية بنسبة 56٪ مقابل . "التدابير الحمائية وبالتأكيد كان لها تأثير على التجارة بين الصين وأمريكا اللاتينية"، وقال سيباستيان بريتو، الرئيس التنفيذي للشركة  "لمواصلة النمو بأمان أعمالهم داخل هذا المناخ التنظيمي الجديد، يجب على كل مستوردى أمريكا اللاتينية تعزيز المعايير الدولية لمراقبة الجودة في مصنعهم. وحقيقة أن ما نراه مع المستوردين هو مسؤولية متزايدة لمنتجاتها وسمعتها التجارية ."

</p>
