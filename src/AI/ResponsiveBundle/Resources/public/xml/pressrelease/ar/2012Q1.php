<h1>مقياس الشركة في الربع الاول : أمريكا اللاتينية،  "حمى البحث عن الذهب" للصادرات الصينية.</h1>
</div>
<div class="para">
<p>
شنتشن، الصين، 16 أبريل،  - الشركة الرائدة في مجال توفير خدمات مراقبة الجودة للشركات المستوردة من آسيا، اليوم مقياس الشركة عن الربع الاول من العام

</p>
</div>
<div class="head-small">
<h2>زيادة الطلب على مراقبة الجودة من المستوردين فى أمريكا اللاتينية </h2>
</div>
<div id="block">
<div id="block-para">
<p>
 زيادة أوامر تفتيش من قبل المستوردين في آسيا أمريكا اللاتينية ارتفع  بمعدل 120٪؛ لا سيما كولومبيا 180٪، بنما٪ 159،٪ 87 وشيلي وبوليفيا 56٪. شهدت البرازيل، أكبر اقتصاد في أمريكا اللاتينية، واحدة من أكبر الزيادات مع أوامر التفتيش بزيادة 240٪ مقارنة بالعام الماضي. هذا النمو، بما يتفق مع زخم متزايد من الاقتصادات في أميركا اللاتينية، كما يوضح إدراك من قبل المستوردين أمريكا اللاتينية من ضرورة تأمين سلامة وجودة المنتج في مصدره. في جو من الشروط، مدفوعا بقوة اقتصادية في البرازيل، ومن المتوقع الناتج المحلي الإجمالي في أميركا اللاتينية عموما بنسبة 4٪ في كل من 2012 و 2013 (المصدر: صندوق النقد الدولي).

</p>
<p>
 في هذا النمو، زادت الواردات الأمريكية اللاتينية بنحو 20٪ في السنوات الأخيرة (المصدر: البنك الدولي). النمو القوي في الطلب على خدمات مراقبة الجودة بالنظر في أصغر اقتصادات أمريكا اللاتينية  في تناقض صارخ بالمقارنة مع الأرقام بالترتيب من أوروبا، التي ارتفعت بنسبة 13٪  متواضعة ، مما يعكس ان اقتصاد الاتحاد الأوروبي مضطرب.

</p>
