<div id="head">
	<h1> 检验数据显示中国仍在快速发展</h1>
</div>
<div class="para">
	<p>
		2012年10月10日，中国深圳 - AsiaInspection，作为亚洲进口业务质量控制服务的领先供应商，公布了其 2012年第三季度报告，这也是亚洲制造业及质量控制服务业的季度汇总。
	</p>
</div>
<div class="head-small">
	<h2>中国检验数据显示，尽管GDP令人担忧，但中国经济仍表现出强劲的增长势头</h2>
</div>
<div id="block">
	<div id="block-para">
		<p>
			AsiaInspection第三季度报告数据显示，尽管在过去三十年来，中国经济的空前增长速度最近有所减缓，但经济、制造业和质量控制行业仍保持稳定增长。并且中国的产品检验季度增长率达到<strong>+23.7%，年增长率达到+12.8%</strong>。
		</p>
		<p>
			当大多数分析师预测中国经济增长速度的减缓有利于东南亚邻国，但这种持续增长与一些持有不同意见的媒体试图提供的观点不谋而合：在经历三十年前所未有的两位数增长后，中国经济放缓不仅是必然的，也许甚至是有益的，这种观点未免有点过度担忧了。
	</div>
	<div id="picture">
		<img src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/img/$file/electrical-inspection.JPG">
		<p>"位于中国的电子产品检验"</p>
	</div>
	<div style="clear:both;"></div>
</div>
<div class="para">
	<p>
		从百分比来看，中国的经济增长的确低于前几年，成熟经济正常进展且不可避免地出现收益递减。从2010到2011年，中国的年度总GDP从6.36万亿增长到7.32万亿美元，<strong>基本上等于墨西哥2011年全年的GDP</strong>。世界银行前任首席经济学家林毅夫预计，在“未来20到30年”，中国经济仍将继续保持高速增长。
	</p>
	<p>
		AsiaInspection首席执行官Sebastien Breteau表示：“孟加拉国、越南甚至印度都缺少中国所拥有的两个硬件条件：14亿劳动力和先进的基础设施，因此无法确保其世界制造商的地位”。他说：“在AsiaInspection，我们认为中国在任何时候都不会放弃其在制造业的主导位置。切记，任何放缓都是为了更大的上升空间。” 
	</p>
	<p>
		除关注中国制造业产出水平外，许多分析家指出，国外需求的萎缩也是导致中国经济增长减缓的主要原因。相比之下，AsiaInspection 2012年第三季度报表数据显示，尽管北美和欧洲经济不景气，但欧洲检验服务的年增长率为<strong>+9%</strong>，G8国家则为<strong>+8%</strong>。
		显示出最强劲年增长势头的地区为南美，达到<strong>+66%</strong>，非洲达到<strong>+39%</strong>。
	</p>
</div>
<div class="head-small">
	<h2>在中国经济显著增长的同时，中国的质量问题仍然存在</h2>
</div>
<div class="para">
	<p>
		尽管中国正在逐渐转化角色为增值服务供应商，但质量控制服务的需求并未降低。美国食品和药物管理局对因来自中国的受污染牛肉干导致2200只宠物疾病和320例死亡事件调查的产品丑闻依然继续。相应的，AsiaInspection数据显示， 2012年第三季度的食品检验需求年增长率为 <strong>+66%</strong>。
	</p>
	<p>
		同样，已召回的中国制造儿童产品已导致七名婴儿头部损伤，包括了在美国和加拿大发现的200多万种沐浴配件。2012年第三季度的玩具检验需求年增长率上升了<strong>+23%</strong>。。在此次激增的召回浪潮中，AsiaInspection数据显示，从今年一月至九月，实验室测试需求较上一年度增加了<strong>+60%</strong>。
	</p>
