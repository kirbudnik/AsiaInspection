<p>AI亚检2018年第一季度报表将为您呈现2017年采购、质量和合规现状以及影响2018年供应链格局的趋势。分析报告数据来源于<a href="https://s3.asiainspection.com/images/news/2018Q1/AI_Q1_2018_Barometer_survey_results_Jan2018.pdf" target="_blank">AI亚检全球供应链年度调查问卷</a>及2017年AI亚检在85个国家执行过的250,000次产品检测、实验室测试以及工厂审核，该问卷由全球超过250个主营各大消费者产品的公司填写完成。</p>

<br />
<h2 style="font-size:18px;">全球供应链趋势：2018年中国与越南扩张在望</h2>
<br />
<p>在国家采购体量方面，<b>中国再一次超额完成目标，超出2017年的预计增长目标6.8%，AI亚检年底记录的产品检测数据显示，中国地区全年发展强健，年增率达9%</b>。中国在AI亚检客户采购目的地选择中依然稳居第一，约三分之二的客户将中国列为首选采购地，有超过一半的客户预计将在2018年加大采购量。</p>
<p>然而，中国在亚太地区的竞争对手也不容小觑，<b>泰国和韩国</b>的迅速扩张尤为瞩目，这两个国家的产品检测和工厂审核的需求在2017年分别上涨了<b>19%和26%</b>。越南在AI亚检客户的采购地域版图中也占有重要地位，鞋类产品是该地区的采购重点，<b>四分之三的AI亚检客户表示2018年将会增加在越南的鞋类产品采购量</b>。</p>
<p>与此同时，在亚洲地区之外的非洲和拉丁美洲也逐渐成为采购商的香饽饽，供应链多样化不断深化。至少有一半的AI亚检客户已经开始在拉丁美洲采购，并将在2018年提高这一地区的采购占比。此外，根据AI亚检2017年底数据显示，南非地区的产品检测需求全年增长11%。</p>

<br />
<h2 style="font-size:18px;">2018年全球买家的挑战：商贸政策不稳定与成本管控</h2>
<br />
<p>采购地域多样性趋势反映了各大品牌商对国际贸易宏观趋势的担忧与顾虑。具体而言，<b>参与受访的客户认为关税、进出口配额、保护主义和贸易禁令等问题在未来都将成为影响供应链的关键因素</b>。制造业全自动化或大数据分析对供应链的影响则被认为还为时尚早，约40%的受访客户认为短期内“不大可能构成影响”。3D打印则显得更为遥远，不到一半的AI亚检受访客户认为这一技术会在未来2-3年内影响业务发展。</p>
<p>在微观层面，<b>制造成本和产品质量将会是2018年供应链采购的两大顾虑</b>。生产和原材料成本这一大供应链难题在2017年最常被提及，AI亚检受访客户认为在接下来一年里，这一问题也将不会得到太大程度的缓解。与此同时，产品质量管控在2018年被视为更大的挑战，尤其是食品行业。</p>

<br />
<h2 style="font-size:18px;">道德合规和供应链可见度令人担忧</h2>
<br />
<p>供应商责任合规在供应链关注榜中排名非常靠后，只有低于20%的AI亚检受访客户将它列入前三大供应链难题。客户对于责任合规情况持乐观态度：大约98%的企业认为供应链责任合规情况在2017年已得到改善或维持现状。<b>然而，根据AI亚检实地获得的数据显示，受访客户似乎过分乐观了</b>。</p>
<p>具体而言，<b>据AI亚检通过工厂审核收集的数据显示，2017年的责任合规情况与2016年相比略有退步</b>，平均责任合规分数下跌了4%。在2017年，严重不合规并得到红色警告的工厂占比攀升到32%（2016年只有27%），而通过审核的工厂仍然维持在33%。<b>各个细分行业在2017年都没有表现出显著的提高</b>，而食品行业的责任合规情况则明显恶化，与2016年相比，平均审核得分下跌了11%。</p>
<p><b>供应商审核执行不规律与低频次</b>造成了客户感知与实际情况的落差：<b>20%的企业没有做过清晰明确的审核，有一部分企业则只在招揽新供应商的时候进行过一次审核</b>。</p>
<p>此外，<b>许多企业都仍在供应链可见度问题上挣扎</b>。只有11%的受访公司自称完全掌握供应商的情况，<b>也就是说还有高达89%的公司仍然存在供应链盲点</b>。在全球范围内，一个企业平均只了解55%的供应商情况，在小公司，这个数据甚至低至38%。</p>

<br />
<h2 style="font-size:18px;">低化学合规通过率影响产品质量，企业需审慎乐观</h2>
<br />
<p>根据AI亚检实地产品检测获得的数据显示，<b>2017年制造业质量略有提升</b>，约23%受检产品没有通过合格测试（2016年为27%）。这一结果与AI亚检受访客户的预期相符，70%的受访客户认为在2017年采购的产品质量有所提升。</p>
<p>在行业方面，<b>纺织、服装和鞋类的质量情况仍然在行业排行榜中垫底</b>，约33%的受检产品没有通过合格测试，虽然与2016年的36%相比已经有了细微的进步。此外，<b>玩具行业的工厂生产质量则有了较明显的改善</b>，与2016年的不合格率相比降低了5%，电子电气行业的表现则保持平稳，约有20%的产品超出了合格标准。</p>
<p>与对工厂生产质量的乐观展望不同，AI亚检客户对实验室测试情况有所保留，只有54%的受访者认为2017年的产品质量和合规情况有所提高。</p>
<p>AI亚检记录的实验室测试数据与客户的预期相符，尤其是出口美国的产品，<b>其2017年的不合格率是2016年的两倍</b>。出口美国的产品检测项目中，8.1%的产品重金属超标，含有过量的铅和镉。9%的产品没能通过违禁领苯二甲酸盐测试。在玩具行业，化学合规仍旧受到非常严峻的挑战：<b>分别有10%和9.4%的出口美国玩具被检出铅含量和镉含量超标</b>。</p>
<p>值得欣喜地是，出口欧盟的产品在2017年有显著提升：分别有3.2%和3.5%的产品没有通过REACH的重金属和领苯二甲酸盐含量测试，比2016年提高了35%。</p>

