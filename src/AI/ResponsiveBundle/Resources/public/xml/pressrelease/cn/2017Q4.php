<p>香港，<b>2017</b>年<b>10</b>月<b>5</b>号- 国际领先的质量控制与供应链合规服务商<b>AI</b>亚检今天发布了<b>2017</b>年第四季度报表：外包制造业、供应链质量和合规趋势季度解读。<b>AI</b>亚检的数据来源于2017年第三季度在全球<b>85</b>个国家执行的<b>250,000</b>次产品检测、工厂审核和实验室测试服务。</p>

<br />
<h2 style="font-size:18px;">全球采购趋势：制造业巨头强势进入节日旺季</h2>
<br />
<p>节日临近，亚洲制造业巨头开始进入旺季，<b>AI</b>亚检接获的产品检验和供应商审核需求也随之提高。中国地区年初至今的产品检验体量年增长达到<b>11.6%</b>，制造业巨头实际增量预计将超过<b>2017</b>年增长预期的<b>6.7%</b>。东南亚地区也增速强健，柬埔寨的产品检验和工厂审核体量在今年以来上涨了<b>22.7%</b>，台湾和泰国本年度体量也在持续上涨。南亚方面，巴基斯坦的产品检验体量在第三季度上涨<b>18.7%</b>，延续AI亚检在上一季度观察所得的强劲势头。</p>
<p>亚洲以外的生产地区也呈现出可持续的发展态势。由于一些品牌注重提高采购地域选择的多样性，拉丁美洲的产品检验和工厂审核需求今年以来上涨了<b>65%</b>。与此同时，非洲的制造商继续强化其在国际纺织市场上的地位，南非和莱索托纺织与服装产品检验体量今年以来分别上升了<b>7.1%</b>和<b>13.6%</b>。</p>
<p><i>“<b>AI</b>亚检足迹遍布全球各大洲85个国家，掌握全球供应链发展态势的一手信息，”</i> <b>AI</b>亚检<b>CEO</b>兼创始人<b>Sebastien Breteau</b>说道，<i> “尽管中国仍然是消费品制造业巨头，但随着各品牌开始注重采购地域多样性、适应宏观经济发展态势，我们看到了全球许多地区百花齐放的强劲发展势头。”</i></p>

<br />
<h2 style="font-size:18px;">地区与行业间差异仍然是道德合规和建筑结构安全的难题</h2>
<br />
<p>旺季物流时间紧迫，这必然也给责任合规带来了压力。<b>AI</b>亚检第三季度的工厂审核数据显示，完全合规（获得绿色得分）的工厂降低到了<b>30%</b>，严重不合规的工厂上升至<b>34.7%</b>，约占三分之一，而2016年只有<b>27.3%</b>。剩下<b>35.3%</b>的工厂收到了黄色警告，需要接受整改。</p>
<p><b>AI</b>亚检数据显示，制造商面临的主要问题仍然是工作时长和薪水合规，这两项平均得分为<b>6.4</b>分（满分10分），而去年底的平均得分为<b>7.3</b>分。</p>
<p>责任审核评分在年初遭遇滑铁卢后，中国工厂这个季度似乎下定决心要稳定社会责任合规情况，平均得分上升到<b>7.5</b>分（满分<b>10</b>分）。与此同时，南亚和东南亚地区的供应商也保持上升态势，与<b>2016</b>年相比，平均得分提高了<b>7%</b>。</p>
<p>在行业方面，家具和电子电气行业责任审核得分表现最佳，平均得分分别为<b>8.1</b>和<b>8.0</b>分，比全行业平均得分<b>7.7</b>分高出许多百分点。玩具行业的道德责任合规情况与去年相比，表现略微糟糕，今年的得分仅为<b>7.4</b>，而去年为<b>8.1</b>。纺织和服装工厂第三季度有轻微下滑，依然落后于全行业平均，只有<b>7.6</b>分，去年为<b>7.8</b>分。</p>
<p><b>AI</b>亚检现场数据显示，第三季度的结构安全明显恶化。与去年底相比，合规的工厂占比没有发生变化，约为<b>32%</b>，但需要立即整改纠正的工厂占比翻倍（<b>2017</b>年至今占<b>6.7%</b>，去年底为<b>2.5%</b>）。中期审核时，<b>61.3%</b>的工厂仍然存有隐患，未能达到完全整改。</p>

<br />
<h2 style="font-size:18px;">产品质量和合规：前进两步，后退一步</h2>
<br />
<p>与去年的数据相比，今年的工厂产品质量得到了较为明显的改善：<b>2017</b>年初至第三季度低于可接受最低质量水平的产品占比与<b>2016</b>年全年相比少了<b>4</b>个百分点。</p>
<p>整体而言，各国家和地区也保持了以往的产品质量水准和趋势。比如，中国的制造业质量有轻微上升，2017年第三季度约有19.7%的产品未达到可接受最低质量水平。南亚的产品质量仍然低下，印度、孟加拉和巴基斯坦地区的产品不合格率在三分之一至二分之一之间。</p>
<p>行业的产品质量同时也反映了各行业的自动化和科技化程度。纺织和服装行业大量依赖劳动力，并且多为低质量劳动力，产品质量表现最差，<b>2017</b>年第三季度数据显示约有<b>30%</b>的产品检验发现了超过可允许瑕疵数量。与此形成鲜明对比的是，同一时期中，科技化程度更高的电子电气行业只有<b>16.9%</b>的产品不符合检测标准。</p>
<p>工厂质量虽然得到改善，但实验室的产品合规情况在第三季度遭遇了重击。在成千上万次消费类产品实验室检测中，<b>AI</b>亚检实验室发现超过<b>12%</b>出口美国市场的产品含有超标的铅，镉及其他重金属（<b>2017</b>年上半年仅为<b>8%</b>），<b>11%</b>的产品含有违禁的邻苯二甲酸盐（<b>2017</b>年上半年只有<b>8.7%</b>）。出口欧洲的产品实验室检测情况相对较好，<b>2%</b>无法通过<b>REACH</b>铅含量检测，<b>5%</b>的产品无法通过<b>REACH</b>领苯二甲酸盐检测，与<b>2017</b>年上半年相比，不合格率减少了一半。</p>
<p>玩具行业的产品合规仍然是项挑战，出口美国的制造商都在努力达到最近发布的美国标准<b>ASTM F963-16</b>。<b>AI</b>亚检在执行遵循此项质量标准的玩具和儿童用品实验室测试时发现，约有<b>8%</b>的玩具无法通过阻燃性测试，<b>9%</b>含有机械危险，超过<b>10%</b>的玩具重金属含量超标。</p>
<p>眼镜行业也同样面临合规困境。<b>AI</b>亚检经<b>HOKLAS</b>授权的实验室进行了一系列光学、化学和性能测试，结果显示<b>12%</b>出口欧洲的眼镜以及<b>28%</b>出口美国市场的产品无法通过一些包括镍释放在内的检验要求。在众多产品类别中，成品眼镜不合格率最高，太阳眼镜排名第二。</p>


