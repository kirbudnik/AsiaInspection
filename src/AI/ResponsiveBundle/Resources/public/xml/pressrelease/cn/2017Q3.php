<h2 style="font-size:18px;">国际采购趋势：中国增速平稳，地区间竞争差距缩小</h2>
<br />
<p>2017年已过去一半，从AI亚检产品检验和供应商审核的实地数据中，仍然清晰可见传统采购大国忙于应对逐步升级的地区竞争的身影。 <b>2017年，中国经济发展增速依旧一马当先，保持在6.5%，其本年度第二季度的产品检验和供应商审核体量也随之同比提高15%。</b> 中国在东南亚的竞争国家也不甘落后，比如越南和柬埔寨第二季度的产品检验需求分别增长了10.4%和25.4%，符合近期对该地区强劲经济发展势头的预测。相比之下，南亚地区的经济发展在今年持续放缓， <b>产品检验与供应商审核需求也较低迷。</b> 唯一例外的是巴基斯坦，在年初经历一个艰难开局后，本季度同比增长6.6%，成了南亚地区的特例。</p>
<p>南非第二季度的产品检验需求同比增长7.7%，<b>以强势增长宣示了其在非亚洲地区服装制造业的强有力地位。</b></p>

<br />
<h2 style="font-size:18px;">社会责任合规和建筑结构安全在2017年备受挑战</h2>
<br />
<p>AI亚检第二季度数据显示，<b>来自不同行业与采购目的地的制造商都仍然在面临社会责任合规问题</b>。 根据报告披露，32.5%的工厂被评定为“红色”—严重不合规（2016年的平均数是27%）。 <b>完全合规的“绿色”工厂百分比下跌至27.5%</b>, 40%的工厂被评定为“黄色”，仍有待提高。</p>
<p>各具体评分项中，<b>员工工作时长和薪水合规以及废弃物处理这两项的平均得分最低，10分只取得了6.7分</b> （2016年末，这两项的平均分分别为7.3和7.1）。</p>
<p><b>这个季度，地区间社会责任合规分数的差距并不显著</b>。 中国制造商得分呈下降趋势，今年以来平均社会责任得分为7.6，与去年相比下降3.8%。</p>
<p>与此同时，南亚及东南亚的供应商今年以来社会合规平均得分高于中国地区，达到8.6， <b>与2016年相比提高了9%</b>。</p>
<p>AI服务的行业中，自动化程度更高及科技应用更广泛的电子电气行业社会责任合规分数最高：10分取得了8.2分，领先于全行业的平均得分7.5分。 <b>而纺织与服装行业的社会责任合规得分自2017年开年起就持续下滑</b>, 2016年平均得分7.8，到2017年第二季度已降低到7.2。这再一次证明，越是劳动密集型的行业，社会责任合规问题就越突出。</p>
<p>"<i>从我们审核员每天在工厂现场采集到的成千上万的数据可以看出，国际供应链的社会责任合规发展仍不均衡，大面积大幅度的改善并不能一蹴而就，</i>" AI亚检CEO及创始人Sebastien Breteau说道， "<i>这是一场持续改进的持久战：如果想要确实长效地改变工厂工作条件，常规的现场跟踪和紧密监管必不可少。</i>"</p>
<p>AI亚检第二季度的数据还透露，世界范围内的工厂建筑结构安全和防火安全也显现出不平衡的发展态势。一方面，合规工厂的占比已爬升至36.5%（2016年为33%），另一方面，<b>4%的工厂被发现有紧迫的安全风险</b> （2016年为2.5%）。 <b>目前，有59.5%的工厂仍在尚未完成整治的状态</b>。</p>

<br />
<h2 style="font-size:18px;">产品质量：工厂发展缓慢，实验室合规状况堪忧</h2>
<br />
<p>AI亚检上半年的数据表明，虽然工厂产品检验的结果显示消费者产品质量在逐渐改善，但在化学成分控制和机械安全方面，产品合规问题仍然是全球消费者产品公司的一大威胁。</p>
<p>根据AI亚检实验室从成千上万消费者产品测试结果中提炼出来的结果，第二季度<b>出口欧盟和美国市场的亚洲产品检测出铅含量超标13%</b> 9%的已测试产品中含有<b>被禁的邻苯二甲酸盐</b>。 11.5%的产品含有超过REACH限定含量或是明令禁止的化学物质，不符合欧盟标准。</p>
<p><b>物理和机械合规</b>在上个季度也是制造商的一大难题，分别有10%和7%出口美国和欧盟市场的产品没能通过<b>机械危险试验和小部件测试</b>。 由于可拆卸小部件在第二季度经常因窒息危险被召回，比如引领最新玩具风潮的指尖陀螺，这方面安全法规的实施将更加严格规范。</p>
<p>工厂产品质量检测结果在这个季度有所改善，今年以来，<b>未通过的产品检验百分比稳定在32.5%</b>, 与2016年同比下降4个百分点。</p>
<p>质量状况发生改善在多个主要的采购目的地同时显现。 <b>上半年，不具备详细说明的中国产品占比31%</b>, 2016年的同期数据是33%。在亚洲大部分主要的采购目的地，拒绝率也下滑了几个百分点。相比较而言，一些像<b>印度、巴基斯坦、孟加拉国</b>这样的南亚主要采购国家，<b>质量仍然是短板</b>，约有49%至54%的工厂产品检验发现产品瑕疵超过限定额度。</p>
<p>行业间的质量状况迥异。劳动密集型的<b>纺织行业产品检验失败率最高</b>，第二季度中大概40%的被检测产品查出低于合格标准。在上半年，<b>玩具和幼儿产品</b>的拒绝率（29%）与去年（31%）相比略有降低。电子电气行业情况相似，2017年上半年拒绝率为25%，2016年同期数据为27%。 <b>配件和眼镜产品</b>今年以来质量状况显著改善（详情参见AI亚检本年度其他报表），渐趋平稳，失败率分别是20%和13%。</p>


