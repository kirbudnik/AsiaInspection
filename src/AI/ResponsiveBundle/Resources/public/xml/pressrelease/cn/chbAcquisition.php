<h1>AsiaInspection收购CHB实验室，加强消费品检测及认证能力</h1>

<img style="float:right;" src="https://s3.asiainspection.com/images/news/CHB_Lab.jpg" alt="CHB lab"/>

<p><b>2013年6月25日，香港</b> - AsiaInspection亚洲品质检验有限公司今天对外宣布已完成对CHB中测环保检测技术有限公司的收购。 CHB是一家中国本土的私营实验室，拥有中国合格评定国家认可委员会CNAS认可、CMA中国国家计量认证证书，并且在REACH 和RoHS测试方面占有领先地位。</p>

<p>AsiaInspection在质量控制领域具有丰富的经验，此次对CHB实验室的收购更是增强了其消费品检测、认证能力。AsiaInpection现具有包括REACH、RoHS 和EN 71 等2000余项实验室测试服务能力，业界领先的48小时的报告周期以及专业的技术咨询、支持，以确保测试产品符合国际标准。</p>

<p>“对CHB实验室及其化学测试（包括电子电器、鞋类、纺织品和玩具）专业知识的并购将会使AsiaInspection的客户大大受益，此次举措将有利于客户利用国际化的专业技能与设施，优化实验室测试网络并进一步降低其在供应链中的风险。” AsiaInspection首席执行官Sebastien Breteau同时也表示，“在CHB，AsiaInspection的客户可受到整个消费品领域的实验室测试协议保护，进一步消除风险。”</p>

