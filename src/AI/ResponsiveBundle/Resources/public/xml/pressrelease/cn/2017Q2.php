<h2 style="font-size:18px;">2017社会责任合规面临一大挑战：合理的工作时长和工资</h2>
<br />
<p>2017年第一季度已经见证了伦理合规方面所面临的巨大挑战, <b>AI数据显示，“红色”工厂，即严重不合规工厂，其比例已经升至30%</b> （2016年该数据为27%）。其余的审核数据分布比较均匀，合规工厂（“绿色”）和需要改进的工厂（“琥珀色”）相差无几，后者略高。 <b>工作时长和工资方面违规最多：</b> 各工厂的平均分是7.9（满分10分），但在这一领域的平均分是7.1。</p>
<p>社会责任表现和趋势在各个地区、各个行业都不尽相同。东南亚供应商得分为8.8分左右，而南亚各个工厂在社会合规领域得分为8.5分（与2016年相比，所有地区在2017年第一季度均增长了8-10个百分点）。与此同时，中国南方工厂的得分要比北方工厂高出11%（分别是7.9分和7.1分）。电子与电气工厂和建材制造工厂也有类似表现。</p>
<p>2017年第一季度表现最好的是食品容器和身体护理行业，各工厂的平均审核得分为8分。玩具制造业的社会责任合规得分在这一季度有所下降，下滑了8个百分点（从2016年的8.1跌至2017年的7.3），健康和安全相关的违规最为常见。</p>
<p>结构安全总体呈恶化趋势，2017年第一季度的AI数据显示，<b>合规工厂的比例跌至30.5%</b> 2016年该比例是37%，2015年该比例为41%）。调查发现，<b>超过65%的工厂需要整改，</b>其中有<b>%的工厂在短期和中期面临着严重的安全风险</b>。</p>

<br />
<h2 style="font-size:18px;">产品质量趋势：各工厂提升甚微，化学合规仍是挑战</h2>
<br />
<p>和2016年相比，2017年第一季度的消费品质量表现没有提升。AI数据显示，<b>审核中的违规率仍徘徊在33%。</b>.</p>
<p><b>中国的制造业产品质量总体上仍需改善，任重而道远。</b> 产品质检的不合格率保持在30%，只比全球平均水平略低几个百分点。 <b>整个南亚地区，超过半数的现场检查中，不合规现象多于合规现象</b>这其中，孟加拉国和印度高居榜首（分别是61.7%和55.3%），印度尼西亚和巴基斯坦紧随其后。相比之下，土耳其制造商在上一季度的产品质量取得了长足进步，审核不合格率降至20%以下，这是过去两年内首次出现的情况。</p>
<p><b>纺织业在质量方面表现依然最为糟糕，</b> 41.7%的产品不符合目标规格。相比之下，<b>配饰行业的质检不合格比例下降至21.3%</b> 与2016年末相比，下降了三个百分点）。眼镜产品是配饰领域的大头，表现出<b>显著的进步：</b>第一季度约有15%的受检产品不合格，而上一年该数字为19%。</p>
<p><b>玩具及青少年产品的质量在2017年第一季度没有明显变化，</b> 拒收率维持在30%左右。 <b>在电子与电气工业领域，</b> 尽管审核不合格比例有小幅下跌（2017年第一季度是23.4%，2016年第四季度是27.8%），<b>安全召回事件依旧层出不穷：</b> 大件家用电器存在爆炸风险，多个大牌手提电脑出现电池故障。臭名昭著的漂浮滑板依然占据头条：最近一次的锂电池故障引发了火灾，导致两人死亡，引发了美国大范围的产品质量调查和召回行动。</p>
<p><b>AI实验室的测试数据</b>表明，消费耐用品制造商在化学合规领域仍有待进步，尤其是铅和邻苯二甲酸盐相关的合规。在所有类型的青少年产品中，这是一个尤为突出的问题。</p>
<p>2017年第一季度，销往美国市场的所有产品中，<b>8%的儿童产品出现了铅含量超标，11%的受检玩具含有禁用的邻苯二甲酸盐，8%的食品接触与包装不符合美国食品药物管理局</b> (FDA）的化学成分要求。同时，出口美国的产品也不容乐观：超过5%的受检产品不符合REACH要求（与2016年相比，增加两倍），因为它们包含的违禁化学品含量已经超出了欧盟市场的标准。</p>
<p><i>AI的首席执行官Sebastien Breteau说道：“产品质量和安全问题仍然是全球各大品牌面临的主要挑战。安全规范在各个层面逐步升级，顾客和媒体的意识有所提升，这些都为我们带来了更大的挑战。甚至是著名品牌也很难在这样一个全球化的市场中持续保持合规水准。</i>"</p>

