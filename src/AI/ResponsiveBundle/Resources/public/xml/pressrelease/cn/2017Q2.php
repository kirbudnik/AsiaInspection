<h2 style="font-size:18px;">采购地理学：中国在2017年强势起步</h2>
<br />
<p>AI第一季度审查和审核数据显示，采购趋势将会贯穿2017年全年，全面影响全球贸易。2017年第一季度，<b>中国的质检总量同比增长了16%</b>, 已经超出了国际货币基金组织预计的6.5%。除了台湾、马来西亚和越南，亚洲其他地区第一季度的增长均显迟缓。上述三地在第一季度的质检总量分别同比增长了20%、10% 和5%。</p>
<p>与此同时, <b>在亚洲以外，南非地区进一步巩固了其纺织品采购枢纽的地位</b>, 2017年第一季度的增长率为23.5%。</p>

