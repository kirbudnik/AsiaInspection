<h2 style="font-size:18px;">越南不畏骚乱</h2>
<p>1 月至 5 月，越南的纺织品和服装出口与去年同期相比增长了 18%，达到了 75 亿美元。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">1</sup></a> 然而，由最近与中国之间的政治紧张局势而引发的抗议活动，则在供应链中产生了恐慌效应。AI 的数据显示，2014 年 5 月验货订单量比 2013 年 5 月至 2014 年 4 月的平均值高出 3.3 倍。这是由于国外采购商对需确保其供应链和产品质量的关注度日益增加。上半年，从越南到美国的服装出口总额增长了 12.7%，远超其竞争对手中国的 0.9%。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">2</sup></a></p>

<h2 style="font-size:18px;">中国正经历史上最大的工业纠纷</h2>
<p style="text-align:justify;">中国政府的刺激措施似乎在第二季度产生了效果。根据来自官方的 PMI 指数最新数据显示，全国的制造业活动和产出恢复了增长。与此同时，在东莞市有超过 40,000 名工人介入了一场当代最大的工业纠纷之中。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">3</sup></a></p>
<p style="text-align:justify;">同时，亚洲检验市场最大的增长来自柬埔寨，与去年同期相比增长了 99%。其次是孟加拉国和越南，各增长 73%，印度增长 63%，中国增长 10%。从行业类别来看，全球食品及食品容器的检验与上个季度相比总体增长了 55%。</p>

<h2 style="font-size:18px;">有毒化学品肆虐：5% 的偶氮染料实验室测试无法通过</h2>
<p>澳洲竞争暨消费者委员会 (ACCC) 于今年三月随机筛选了一些进口服装进行偶氮染料测试。其中大约有3% 含有可能会危害人体健康的致癌物质，最终导致超过 200,000 件商品从零售商店中被主动召回。这涉及了来自不同零售商的 37 条生产线。通过对内部数据的分析，AsiaInspection 发现在 2014 年所有的偶氮染料实验室测试中，有 5% 以上由于超过法定最低含量而无法通过。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">4</sup></a></p>
<p>“一次又一次，我们关注到外包制造的产品将消费者安全问题置于风险之中。事实表明，超过 200,000 种位于商店货架上的产品包含一种已知的致癌物质。这就需要从源头上进行更严格的控制和检测。这也反映了大量的消费者正处于风险之中。”AsiaInspection 的首席执行官 Sebastien Breteau 说。</p>
<p>采购期刊（The Sourcing Journal)报道，虽然越来越多地品牌商和零售商将价值链中的信息置于极其重要的位置，但具体的信息往往是非常难以取得的。50% 的受访者表示，他们并不能够从他们的供应链中获取足够的数据。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">5</sup></a></p>

