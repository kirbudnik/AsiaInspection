<h2 style="font-size:18px;">非洲增势强劲</h2>
<p style="text-align:justify;">亚洲验货订单量增长依然强劲，与去年同期相比增长了 21%。但这已被非洲 31% 的检验订购增长所超越。有趣的是，非洲的检验增长由斯威士兰引领，仅上个季度就将增长了 50%。与此同时，根据《非洲增长与机遇法案》，由于斯威士兰在国际公认的劳工权利方面未取得进展，已失去其来自美国的利润。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">6</sup></a></p>

<h2 style="font-size:18px;">印度挑战中国，成为世界第二大纺织品出口国</h2>
<p>印度正在崛起形成一股区域力量跻身世界第二大纺织品出口国的地位，并占据了全球份额的 5.2%。印度纺织部长预计本财政年度的出口总值将达到 500 亿美元。这种增长很大程度上受其服装制造业的推动。其 5 月份的出口额增长了将近 25%。服装出口的增长正受非传统市场（如拉丁美洲、非洲南部、东亚和西亚）的推动。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">7</sup></a></p>

<h2 style="font-size:18px;">检验团队在孟加拉国引发紧张局势</h2>
<p>北美和欧洲的检验团队“联盟”和“协调”一起审核了孟加拉国 5,600 家制衣厂中的 2,100 家。这些工厂雇佣了大约 400 万工人，并占据了该国 80% 的出口额。AI 2014 年的数据表明，在进行过社会责任审查的工厂中，有 27% 处于严重风险中，而有 59% 则不合规。</p>
<p>两个团队参与了与利益相关人进行的一场讨论，内容是关于他们关闭不合规工厂的流程。有人担心，这样的中断可能会导致出口增长放缓，而买家则会将目光转向他们认为更稳定的货源，如中国、印度和越南。检验团队在现场保护工人利益的同时，还有迫在眉睫的实际问题 - 就业机会的保障，以及停工维修延长时工人报酬的发放。这些往往会在没有事先通知的情况下发生。到目前为止，14 家工厂曾被关闭，还有 5 家的经营受到过限制。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">8</sup></a></p>
<p>工厂关闭应当仅在雇员有直接风险时，才能进行。AI 的经验表明，在一次正确执行的审核程序中发现的许多错误，都可以通过纠正措施、培训和教育来得到更正，从而促进工厂和国家长期经济价值的发展。</p>

