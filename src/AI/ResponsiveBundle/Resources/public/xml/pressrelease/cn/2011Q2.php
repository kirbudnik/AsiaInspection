<h2>AsiaInspection Audit Figures Reach All-time High</h2>
</div>
<div class="para">
<p>
Audit figures for Q2 2011 were up a remarkable +57% year-over-year, and +29% from Q1. According to AsiaInspection CEO Sebastien Breteau, these figures suggest more importers are investing in their suppliers for the long term.
"The growth in audit services may mean that more importers recognize the long-term value of helping to improve the quality of their supplier's operation,
and not just the product that they are going to sell next month. Assessing the condition of factory equipment, staff and facilities goes a long way towards improving product quality once production starts."
</p>
