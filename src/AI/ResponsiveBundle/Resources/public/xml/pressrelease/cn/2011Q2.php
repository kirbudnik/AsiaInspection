<h1>AsiaInspection Q2 2011 Barometer: China Races to Secure Venezuela Oil with Exports</h1>
</div>
<div class="para">
<p>
Shenzhen, China, July 13, 2011- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
today announces the AsiaInspection Q2 2011 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
</p>
</div>
<div class="head-small">
<h2>China Sees Higher Trade Surplus, Inspections Follow Suit</h2>
</div>
<div class="para">
<p>
In Q2 2011, AsiaInspection service figures in China increased +27% year-over-year, continuing to outpace export growth which grew 20% from year-earlier periods.
China's growth comes in the wake of rising worker wages, continued inflation and supply chain disturbances due to the March earthquake in Japan.
AsiaInspection inspection figures also show large increases in Asia's other top export markets, where inflation is less rampant, with Indonesia up 75.7%, Bangladesh 65.8% and Vietnam +53%.
</p>
</div>
<div class="head-small">
<h2>China Applying its Mideast "Exports-for-Oil" Model in South America</h2>
</div>
<div id="block">
<div id="block-para">
<p>
Bangladesh, with inspection figures up +65.8%, has one of the lowest worker wages in Asia.
Foreign direct investment proposals have jumped from $584 million USD to $3.83 billion in the last fiscal year, in large part because of the higher wages in China, strengthening of the Chinese Yuan and in recent quarters,
higher tariffs imposed on China from select developed countries versus other Southeast Asian countries.For example, China-made LEDs are imposed a 6 percent tariff when imported into the US, while those made in Vietnam are duty-free,
opening the door for China itself to finalize production outside of China, to mitigate imposed tariffs.
</p>
<p>
In addition to a +14.4% increase in the traditionally strong Textile and Apparel category, AsiaInspection inspection figures show products that were sluggish in 2010 make a comeback.
The greatest rebounds were seen in Food and Food Packaging up +98%, Homeware and Gardenware +66%, and Construction and Building Materials +37%.
</p>
