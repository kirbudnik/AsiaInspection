<h1>AsiaInspection Q4 2009 Barometer: As Economy Improves, Social Conscience Returns</h1>
</div>
<br/>
<div class="head-small">
<h2>Importing Nations Leading China to Social Responsibility</h2>
</div>
<div class="para">
<p>
Figures released by the quality control industry in Asia are good indicators of the health of International Trade.
Based on a few of these indicators,
<a href="/">AsiaInspection</a>
publishes this barometer on a quarterly basis. With over <strong>9,500</strong> inspections performed during the fourth quarter of 2009
<strong>(+21% from Q4 2008)</strong> , AsiaInspection has identified some interesting trends.
</p>
<p>
Based on an end-of-the-year study conducted by the Chinese Government, there is plenty of cause for concern.
According to the study, nearly <strong>80%</strong> of private Chinese firms violate employee rights and have
not signed contracts with their employees. This, in the country with the world's largest labor force at approximately
800 million strong.
</p>
<p>	   With this,
<a href="http://www.asiainspection.com/ethical-audit">Social Audits</a>
soared <strong>+75%</strong> from <strong>Q4 2009</strong>, compared to an <strong>+8% </strong> growth in <strong>Q3 2009</strong>.
This surge reflects the growing sentiment consumers are feeling regarding social responsibility in the workplace.
This growth also indicates that social pressure by consumers and international organizations results in direct action by importing companies.
</p>
<p> An increase in
<a href="http://www.asiainspection.com/manufacturing-audit">Factory Audits</a>
of <strong>+66%</strong> from <strong>Q3 2008</strong> indicates that consumers continue to demand higher
quality imported products. Companies doing business in Asia are making quality assurance a priority in order
to protect their brand. This attitude has resulted in a fourth quarter virtually free of major product scandals.
</p>
<p>
The 2009 holiday season saw double-digit increases of inspections in nearly all product categories. Inspections of toys increased by
<strong>+56%</strong> and electronics grew by <strong>+42%</strong>.  Inspections of clothing also rose by <strong>+67%</strong>,
indicating that a tax refund on textiles implemented by China in early 2009 has been successful in increasing export volume.
</p>
</div>
<br/>
<br/>
<div class="head-small">
<h2>Importing Countries Continue to Rebound More Slowly</h2>
</div>
<div class="para">
<p>
As in <strong>Q3 2009</strong>, Asia and the Middle East continue to lead the world with +81% and +137% ordered inspection growth. The United States and Canada are rising out of their economic slump more slowly,
<a href="http://www.asiainspection.com/quality-control-services-china-india-asia#product_inspections">inspection services</a>
ordered in Asia increased by a modest <strong>+1.8%</strong> from Q4 2008, but remained unchanged from last quarter. Eastern Europe has not yet pulled itself out of the global recession with a
<strong>37%</strong> decrease. Western Europe is recovering more quickly, with an increase of <strong>26%</strong>.
</p>
