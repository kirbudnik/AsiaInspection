<h2 style="font-size:18px;">中国屡陷食品安全危机</h2>
<p style="text-align:justify;">中国上季度发生的几宗食品丑闻使得各品牌增加了改进食品安全的压力。其中最大的丑闻莫过于百胜及麦当劳使用过期肉的消息。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">1</sup></a> 在另一食品安全事件中，警方查获了30,000吨浸泡于过氧化氢中的鸡爪， <a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">2</sup></a> 另外今年九月一集团因非法贩卖超过九千万明胶胶囊丸被逮捕。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">3</sup></a></p>
<p style="text-align:justify;">这一食品安全危机在AsiaFoodInspection（AI的食品质量控制服务部门）数据中有所体现，食品检验订单量比上一季度增长21.3%，年增长率为30%。AFI数据显示，2014年第一季度跟第三季度期间，在中国51.7%的食品检验是不合格的。</p>
<p>“中国制食品频频将消费者置于安全危害的现象是完全不可接受的”，AsiaInspection的首席执行官Sebastien Breteau指出：“长远的根本解决方案是加强政府法律法规管制以及在源头控制食品的检验和测试。”</p>
<p>中国食品安全法案正在进行重新起草，责任及处罚显著增加，此举将会给生产商施加压力以证明其产品符合食品安全标准。这一新的系统涵盖的奖励制度也将促进今后消费者的举报检举行为。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">4</sup></a></p>

<h2 style="font-size:18px;">中国加强高科技创新产业</h2>
<p>中国以政府支持的“中国制造”计划回应了9月推出的“印度制造”计划。印度计划，旨在促进高科技制造业，但是并没有提供具体的奖励措施。相比之下，中国计划不仅针对重点行业(如消费类电子行业)提供高科技设备减税，同时提供了税收优惠以鼓励工厂改进自身机器。</p>
<p>中国电子制造业已经发展成熟，但标准不一，AI 2014年第三季度数据显示，24.5%的电子产品检验超过了AQL（可接受质量水平），相比去年同期增长9.1％，但与印度27.8%的数字相比，中国仍占有优势。</p>

<h2 style="font-size:18px;">社会责任审核让人们了解不安全的工作环境</h2>
<p>社会责任审核在中国一直备受关注，特别是近日新安全生产法对恶劣工作环境进行了更严格的惩罚。这包括对严重工作场所事故处罚超过320万美金的罚金，以及最高扣发经理80%的工资。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">5</sup></a> </p>
<p>本年度迄今为止，由AI在中国实行的社会责任审核由于重大问题未通过的比率是26.4%，相较2013年的22.9% 有所增长。这个数据与印度的28.6%相近。AI数据显示卫生，健康，安全和工作时间，工资，福利是在中国最普遍的导致社会责任审核失败的原因。</p>

<h2 style="font-size:18px;">缅甸增加5万服装工人</h2>
<p>在缅甸最近的经济自由化后，AI在2014年第三季度观察到该地区对于产品检测的需求有所增长，特别是在布料和服装行业。利用“中国+1”采购策略，缅甸现在成为采购商减少在中国花费的可行选择。缅甸的日工资大约是1.1美金，越南2.5美金，孟加拉国2.8美金，而中国是18美金。缅甸政府正在逐渐支持边境事务部（MBA）一个项目，旨在培训5万名服装工人，其中2万人已被成功地安置了工作。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">6</sup></a> </p>

