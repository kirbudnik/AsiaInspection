<h2 style="font-size:18px;">尽管部分工厂被关，孟加拉国出口仍呈增长趋势</h2>
<p style="text-align:justify;">孟加拉国服装制造商和出口商协会（BGMEA）表明超过200家孟加拉国工厂在去年拉纳大厦（Rana Plaza）工厂事件后被关，这造成上百万出口额和成千上万工作机会的损失。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">7</sup></a></p>
<p style="text-align:justify;">这种趋势在标准提升前可能一直保持。AI数据显示尽管本年度迄今为止审核的数量有所上升，在孟加拉国仍然有38.7% 的生产和16.4% 的社会责任审核均未通过，2013年相应的数据分别是23.9% 和 11.1%。而出口量却在一月到五月间比去年同期增长了13%，其中66%的产品出口至欧洲和美国。<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">8</sup></a></p>

