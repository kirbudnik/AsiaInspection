<h2 style="font-size:18px;">为了应对道德和可持续性供应链问题，许多品牌选择长期供应商合作</h2>
<br />
 
<p>AI 发现，2016 年由其负责审核的工厂中有 <b>63% 需要整治</b>，而合规工厂的数量已从 2015 年的 41% 下降到 37%，结构安全的形势依然严峻。另外，AI还发现同年有3% 的工厂存在重大问题，与上一年相比略有改进。</p>
<p>与此同时，道德合规性持续面临挑战：从AI 道德审核中收集的数据显示，2016年只有三分之一的工厂合规。虽然严重不合规的供应商数量有所下降（2016 年为 27%，2015 年为 41%），但大部分供应商的评级仍为“琥珀色”，表示需要中长期的改进。在道德合规性中，最突出的问题是一系列劳动者侵犯行为，包括强制加班、性骚扰、童工、强迫劳动和剥削难民</b>.</p>
<p>在 2016 年的全球供应链中，环境合规性情况也参差不齐。据 AI 数据显示，今年超过 36% 的审核未通过，相比 2015 年增加 5%。整体而言，许多品牌没有从一开始就做好为达到 ISO 14001 合规标Neverthel准的准备，而是选择以采购国家和地区适用的最低法律标准为目标。</p>
<p>尽管如此，越来越多的公司致力于实现短期和长期的可持续性目标，为 2020 年及以后设定目标。AsiaInspection 创始人兼首席执行官 Sebastien Breteau 表示：“供应商意识与合作是进步的关键所在。我们已经制定培训与研讨会方案，为客户和供应商提供与道德、结构和环境合规性相关的培训，并预计 2017 年类似培训的需求将增加。” </p>

<br />
<h2 style="font-size:18px;">产品质量趋势：总体结果令人担忧，各国各行业情况对比鲜明</h2>
<br />
<p>总体而言，2016 年全球消费品制造质量均有所下降：与 2015 年相比，2016 年<b>AI 的工厂产品检验未通过率提高 8.8%</b>，其中 35.8% 的检验超出了可接受的缺陷水平。</p>
<p><b>中国的产品制造质量相对较好</b>，同比保持稳定，其中 32.4% 的现场检验结果超出允许的缺陷水平，而越南的情况表现为平稳，检验未通过率从去年的 35.1% 下降至 29.0%。相比之下，<b>南亚地区的产品制造质量均显著降低</b>，印度、孟加拉国、巴基斯坦等国的检验未通过率增长达两位数。</p>
<p>在 AI 数据所涉及的行业里，质量问题在纺织行业最为常见，在受检产品中，有 41.6% 不合规。而玩具和婴儿产品的制造质量略微提升，不合格率下降 3 个百分点，为 30.3%。</p>
<p>与工厂的产品制造质量水平下降相比，2016 年实验室的产品安全和性能测试的结果良好：2016 年，AI 实验室测试中共有 3.3% 的测试未通过，未通过率总体下降 11.4%。虽然各大品牌和制造商看似越来越擅长在遵守国际标准的前提下正确设计和构思自身产品，但到了批量生产时许多质量问题便开始纷纷涌现。</p>
<p>此外，回顾过去一年，虽然实验室测试结果数据整体而言令人欣慰，但也体现了更加迥异的现实。例如，今年欧盟对 REACH 相关测试的需求剧增，而测试结果却并不理想，未通过率是去年的三倍。另外我们还发现，未通过率最高的是邻苯二甲酸盐相关测试，多达 5.4% 的测试不符合安全要求，表示其可能为有害产品。</p>

