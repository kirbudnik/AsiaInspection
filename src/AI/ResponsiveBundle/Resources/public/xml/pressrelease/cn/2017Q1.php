<p>全球领先的质量检测与供应商合规性审核服务企业 AsiaInspection (AI) 于今日发布了 2017 年第一季度晴雨表：有关外包制造、供应链质量与合规性趋势的季度见解。AI 数据以 2016 年在 77 个国家和地区进行的 250,000 次检验、审计和实验室测试为依据。</p>

<br />
<h2 style="font-size:18px;">采购地区：中国反弹，南亚激增</h2>AI 
<br />
<p>AI 2016 年审核和检验需求表明了各采购地区对全球品牌和买家的吸引力。在经历了因工作成本增加和硬通货造成的数季度增长缓慢后，<b>中国在 2016 年再一次加快速度（同比增长 12.5%）</b>）。甚至超过了部分竞争对手，其中越南同比增长 5%，较为缓和，而泰国则同比持平。</p>
<p><b>作为制造业重点地区，对南亚地区的采购需求继续增长，尤其是纺织行业，以印度和巴基斯坦为例，2016 年其审计与检验量分别增长 30.5% 和 18.2%。</p>

