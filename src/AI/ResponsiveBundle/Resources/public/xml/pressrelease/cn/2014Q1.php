<h2 style="font-size:18px;">中国出口总值创高峰</h2>
<p style="text-align:justify;">中国于春节期间，二月份出口总额比2013同期下降18%。媒体报告指出，中国经济走势开始逐渐缓慢。 </p>
<p style="text-align:justify;">值得关注的是，中国在众多采购区域及国家中就总额而言仍占主导地位。其中，去年十二月至今年一月出口总额再创新高并超出2,070亿美元<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">1</sup></a>。相对而言，撇除中国春节假期时段，2013年十一月至2014年一月期间，中国出口总额较2012/2013同比增长<b>+9%</b>。</p>
<p style="text-align:justify;">AsiaInspection报告数字显示中国境内验货年增长率超出该出口总额数值（+9%）以<b>12%</b>的年增长率增长。根据更进一步的数字显示，今年三月较去年同期增长<b>+26.2%</b>。</p>
<p style="text-align:justify;">导致出口率增长的原因何在？于第一季度期间，人民币汇率自2005年以来首次呈现增长停滞趋势并下降2.7%。对正在寻找新兴制造国市场的进口商而言，这正符合“中国+1”的政策：将主要制造业保留在中国。中国将持续在工人薪资、相对发达的生产基础设施以及世界最大的劳动力市场方面保持优势，平衡发展。</p>

<br style="line-height:8px;" />
<h2 style="font-size:18px;">"中国+ 1"投资政策</h2>
<p style="text-align:justify;">南亚及东南亚国家为品牌商提供降低劳动力成本的机会。虽然他们各自的经济规模要小很多，新兴市场国就增长率而言却是明显的赢家。 孟加拉验货的年增长率为<b>+70%</b>，越南为<b>+57%</b>。柬埔寨服装业虽受全国各地局势动荡影响，譬如今年一月，抗议示威不断升级，警方开火镇压局势已造成4人死亡，38人受伤<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">2</sup></a>。尽管如此，柬埔寨验货增长率仍达<b>+32%</b>。在印度尼西亚，纺织业已成为该国的最大出口类，验货率增加<b>+22%</b>。</p>

<h2 style="font-size:18px;">品牌商及进口商采取措施以掌控其供应链</h2>
<p style="text-align:justify;">现今已经有越来越多的消费者意识到对质量的要求不仅是针对高质量的产品，更是针对高质量的供应链。</p>
<p style="text-align:justify;">AsiaInspection 2014年第一季度报表显示亚洲的供应商道德及生产审核增长率为<b>+156%</b>。拉纳广场（Rana Plaza）事件曝露出制造厂商如何非法将订单外包，而零售商则发现其产品并未授权于那些外包制造的现象。为避免此类现象再度发生，品牌商采取对策，以供应商的社会道德审核及现场验货相结合的方式，确保生产过程是在已审批的工厂中进行。</p>
<p style="text-align:justify;">AsiaInspection的首席执行官Sebastien Breteau表示：“全球供应链的一个显著现象是：企业一方面竭尽全力获取最大利润，而另一方面，政府则加强各项法规条例，以确保其社会道德符合标准。” </p>
<p style="text-align:justify;">美国商会就400个从中国进口的成员公司进行调查，发现40%有意于社会道德审核投资更多成本<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">3</sup></a>。欧洲企业则面临更严格的审核标准，要求他们就针对整个供应链的道德问题，贪污腐败及人权安全性隐患采取政策措施<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">4</sup></a>。</p>
<p style="text-align:justify;">中国并没有从社会道德符合性问题中豁免。于今年第一季度，16名工人在位于杭州的一场工厂火灾中死亡<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">5</sup></a>，11人在广东省汕头市一火灾死亡<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">6</sup></a>。</p>

<h2 style="font-size:18px;">非洲，一个新兴采购区</h2>
<p>低成本高强度的生产制造业已经在非洲产生。AsiaInspection的验货订单年增长率为<b>+65%</b>。美国服装和鞋类协会（AAFA）报告显示，越来越多的生产商愿意在更接近于消费者市场的南非，而并非亚洲，进行生产投资<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">7</sup></a>。非洲亦是未来20年最具人口增长潜力的区域，为以长远发展为目标的企业提供庞大的生产劳动力及终端客户。国外首要投资对象国家──埃塞俄比亚，将2014年全年出口总额达1亿美元作为其4年发展及转型的目标<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">8</sup></a>。</p>

