<div class="head-small">
<h2>AsiaInspection barometer: Chinese fashion production is trendy again</h2>
</div>
<div class="para">
<p>
The figures from the <strong>Quality Control</strong> industry in Asia account for a good indicator of the health of International Trade. Based on a few selected indicators,
<strong>AsiaInspection</strong> publishes this barometer on a quarterly basis. With over <strong>9,000 Inspections</strong> performed during the 3rd Quarter of 2009 (<strong>+ 32%</strong>
vs. Q3 2008),<strong>AsiaInspection</strong> emphasized some interesting trends.
</p>
</div>
<div id="block">
<div id="picture">
<img width="300" align="left" src="http://asiainspection.com/qi/aiweb.nsf/WebRessources/2kStaticPageImg/$file/Inspection_Services_Q3_EN.png" style="padding-right:15px;">
</div>
<div id="block-para">
<p>
The Chinese government, in an effort to stimulate the economy, increased tax refunds on certain exports. The results were positive; book and stationery
<strong>Inspections</strong> were up <strong>+24%</strong>, toys <strong>+32%</strong>, shoes and fashion accessories <strong>+58%</strong>
and textile apparel <strong>+63%</strong>. As of January 2009, the European Union removed a barrier forcing companies to source a percentage of their textile business from within the EU.
This coupled with the tax refunds led to this impressive growth in the textile and garment exports.
</p>
</div>
<div style="clear:both;"></div>
</div>
<div class="para">
<p>
Growth in <strong>Factory Audits</strong>, to the tune of <strong>53%</strong>, stresses the demand by consumers for higher quality products.
Companies dealing in foreign countries, particularly China, are increasingly concerned with protecting their brand after a string of product scandals.
</p>
<p>
The Chinese government spent over 7 million RMB in the third quarter of 2009 to increase food and drug oversight in an attempt to improve consumer safety.
In recent years, Chinese suppliers have had numerous scandals putting consumers at risk. This is a clear attempt to improve consumers' safety and Chinese exporters' reputation.
</p>
<p>
However <strong>Social Audits</strong>, hough up 8%, have not followed Factory Audits. This statement indicates that importers worldwide are less concerned with social responsibility.
</p>
</div>
<div class="head-small">
<h2>Exporting countries, putting the world back in business</h2>
</div>
<div class="para">
<p>
Though consumer discretionary spending was hit hard during the global economic recession, <strong> Product Inspections</strong>
for electronic goods were up 21% in the third quarter, another indicator the economy is on the mend.
</p>
<p>
Eastern Europe continues to be deeply impacted by the recession;<strong>Inspection services</strong>ordered in Asia were down
<strong>54%</strong> in the quarter, while neighboring Western Europe saw a healthy rebound with <strong>29%</strong>  growth.
The United States finally stabilized and recorded a slight increase of <strong>1.8%</strong> from Q3 2008 to Q3 2009.
Emerging markets in Asia and the Middle East continue to lead with <strong>81% </strong>and <strong>123% Inspection</strong>
growth in the quarter.
</p>
