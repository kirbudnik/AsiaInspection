<p style="text-align:justify;">2013年对于全球的品牌商和零售业来说是面对挑战的一年。以拉纳广场（Rana Plaza）事件为例，他们面对了越来越多的供应链社会问题。拉纳市场事件是指今年5月孟加拉国一家工厂倒塌夺走1,129条生命的悲剧。</p>
<p style="text-align:justify;">这一年罢工和暴动遍布印度、孟加拉、柬埔寨、缅甸的工厂，工人纷纷要求提高工资。在孟加拉，工人的工资是中国工人的一半；目前柬埔寨是每月80美元/月，将在2014年4月增加到95美元/月。</p>
<p style="text-align:justify;">在美国劳工部刊出关于亚太地区7800万童工<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">1</sup></a>的报告<a href="#footnotes" style="text-decoration:none;"><sup style="font-size:10px;">2</sup></a>之后，童工问题再次被放到聚光灯下。</p>
<p style="text-align:justify;">AsiaInspection的数据反映出各地的品牌商和零售商正在采取行动，与2012年相比，2013年AsiaInspection亚洲地区的审核订单比去年增长了<b>+61%</b>。孟加拉国仍然在聚光灯下，但买家在所有采购地域纷纷采用审核的方式确保各自供应链的安全。较去年同期，孟加拉国增长<b>47%</b>、中国增长58％，印度的增长高达112％。</p>
<p style="text-align:justify;">由于在孟加拉国的悲剧事件及其以服装制造行业为主导的状况，消息传出买家开始寻求在该地区的替代。AsiaInspection 验货订单的数据也支持了这一说法。孟加拉国作为仅次于中国的服装最大出口国，验货订单比往年增长了<b>+36%</b>，而印度超过了这个数字达到<b>+44%</b>，越南 <b>+71%</b>，柬埔寨增长量高达<b>+114%</b>。</p>
