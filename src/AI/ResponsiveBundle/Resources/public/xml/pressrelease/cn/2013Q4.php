<h2 style="font-size:18px;">圣诞季刚过 中国制造的玩具安全了吗？</h2>
<p>中国继续主导玩具制造行业，在西方国家出售的逾<b>80%</b>的玩具是中国制造。圣诞季刚过去，消费者的安全被提上最后一个季度的日程。圣诞季的玩具销售高达所有销售活动的<b>70%</b>。</p>
<p>11月，200,000中国制造的玩具在美国港口因含有含有高浓度的邻苯二甲酸盐（在塑料中使用违禁化学品）而被查获。同样的案例也发生在法国，12月份有30,000件不达标的中国玩具被海关销毁。与此同时，美国公共利益研究集团对50种玩具进行分析发现，铅含量超出安全限制29倍；禁止使用的邻苯二甲酸盐被运用，镉超标以及玩具具有窒息的风险。</p>
<p>AsiaInspection在玩具产业的数据显示了令人担忧的问题。2013年在亚洲玩具检验的不合格率从2012年的<b>27%</b>上升到<b>33%</b>。大多数不合格的验货问题属于小的并不危及生命，但也有超过<b>4%</b>的在AI的玩具检验没有通过相应的安全标准。</p>

<h2 style="font-size:18px;">采购力：中国 vs 世界其他国家</h2>
<p>尽管市场担心中国的竞争力会受人民币升值及工资上涨的影响而下降，中国的出口量屡创新高。11月同比增长12.7％，1月至11月较2012年增长<b>9.3%</b>。这使中国达到继2008年以来最大的年度贸易顺差。</p>
<p>AsiaInspection验货数据也印证了这点。2013年AsiaInspection在中国的验货订单量较2012年上涨了<b>17%</b>。中国仍是AI客户的最大的采购区域，而孟加拉国出口总额仅为中国的<b>1%</b>。</p>
<p>从百分比增长来看，2013年采购区域有新的变化趋势。非洲今年下半年出口量增加了<b>47%</b>，越南增长<b>71%</b>。尽管有劳工对工资抗议，柬埔寨的数据仍翻倍。</p>

<h2 style="font-size:18px;">展望2014年全球采购发展趋势</h2>
<p>2013年的供应链管理问题，不仅引起采购相关人员高度重视，更引起了消费者的关注。拉纳广场（Rana Plaza）事件悲剧，欧洲马肉丑闻以及各食品安全问题的案例在中国不断重演使得全球食品安全意识提高并且加强了对更严格的端到端的全球供应链管理的需求。</p>
<p>“西方采购分公司不了解他们产品的产地以及外包工厂状况的现象频频出现，这种情况注定了失败”，AsiaInspection的首席执行官Sebastien Breteau说，“知名品牌H&M, 苹果和惠普已经公开部分乃至完整的合格供应商。我们将会看到更多的以知名企业为首的此类举措，对供应商采取完全透明化控制。预计2014年全球供应链中的透明化控制将成为现实。”</p>

