<h1>全球供应链具有社会责任的一年？</h1>
<p><b>2014年1月14日</b> - 中国深圳-AsiaInspection今日发布2013年第四季度报表，发表年度性的外包制造业以及质量控制服务行业评论。AsiaInspection是领先的质量控制服务供应商，我们与国际品牌、零售商及进口商建立合作关系，帮助他们管理在亚洲、非洲、南欧及拉丁美洲的供应链并保障其供应链安全。</p>

<h2 style="font-size:18px;">拉纳大厦（Rana Plaza）事件之后的亚洲制造业</h2>
