<p>AsiaInspection首席执行官Sebastien Breteau表示，“我们业务的核心在于，客户能够更为便捷地统筹并管理他们所需的任何测试或检测，因此，我们很高兴能将AsiaInspection模式带给更多的受众”。</p>

<p>“我们与Anseco一样，崇尚客户至上的企业文化。我们所有的客户，无论是当前还是潜在客户，都将从我们这两家不断创新、朝气蓬勃、致力于为客户提供最好服务的公司联合中受益。”</p>

<p>此次收购将为AsiaInspection新增两所获认证的室内实验室，有助于进一步提高AI向北美和亚洲的客户提供技术支持的能力；新增的两所实验室为纽约布法罗市实验室和香港实验室，其中前者已获ISO17025认证，后者已获香港实验所认可计划（HOKLAS）认证。</p>

