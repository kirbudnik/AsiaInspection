<div style=" margin-bottom:10px;padding:5px;margin-right:10px;background-color: #EEEEEE;"><span ><i>AsiaInspection近期与Anseco集团达成收购协议，此举将有效促使全球玩具及产品制造商更好地享受以消费者为中心的服务以及权威认证的实验室专业鉴定。</i></div>
<p><b>2015年9月22日，香港</b> 测试产品的安全性对所有厂商都至关重要，消费者的健康和安全是重中之重，行业对此采取严格标准。</p>



<p>作为一家迅速发展的国际测试、检测和审核公司，AsiaInspection近日宣布收购Anseco集团，将可为厂商提供更为便捷的产品测试供应商选择通道。Anseco总部位于美国，是一家专营玩具、儿童用品和轻工业产品实验室测试的公司。</p>

<p>AI对Anseco的收购将使玩具、儿童用品和轻工业产品制造商受益，自此制造商不仅可以获得ANSECO的专业实验室支持，还可以享受AsiaInspection引领市场的在线预约和在线报告服务。网络平台可以使零售商和品牌商在任何设备上实时确认、取消或查看产品测试结果，并运用这些数据即时分析得出可持续为其供应链提供最佳支持的工厂。</p>

