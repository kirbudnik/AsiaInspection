<div id="head">
	<h1> The Positive Momentum of China-Africa Bilateral Trade.</h1>
</div>
<div class="para">
	<p>
		Shenzhen, China, July 9, 2012- AsiaInspection, a leading provider of quality control services for businesses importing from Asia,
		today announces the AsiaInspection Q2 2012 Barometer,a quarterly synopsis of Asia-based manufacturing and the quality control services industry.
	</p>
</div>
<div class="head-small">
	<h2>Africa: a Growing Destination for “Made-in-China”</h2>
</div>
<div id="block">
	<div id="block-para">
		<p>
			AsiaInspection’s Q2 figures indicate that Africa is a growing importer of Made-in-Asia products, particularly Made-in-China.
			The growth in demand for quality control in Asia from African importers outperformed that of all other regions by more than
			20%, increasing +23% from the first half of 2011 to the first half of 2012.  According to AsiaInspection data, most active importers
			are found in North Africa (Morocco, Tunisia, and Egypt),South Africa and Nigeria, with a notable uptick in lesser known countries Botswana,
			Ghana and Ethiopia.
		</p>
		<p>
			These findings are representative of global data regarding China - Africa trade which saw 2010-2011 imports from China increase 23.7%
			to $73 billion USD (source: Standard Bank Group). Over the past few years, China has become Africa’s largest individual trading partner,
			with trade volumes exceeding $160 billion USD in 2011.
		</p>
	</div>
	<div id="picture">
		<img src="http://www.asiainspection.com/qi/aiweb.nsf/webressources/img/$file/china-africa-factory.jpg">
		<p>"Chinese and African workers in a factory in Africa"</p>
	</div>
	<div style="clear:both;"></div>
</div>
<div class="para">
	<p>
		AsiaInspection data, based on manufactured products inspected in Asia, shows total African imports from China are dominated by textiles and footwear.
		In the first half of 2012, these categories made up 80% of all products inspected in China by African importers.
		While lower in total size compared with textiles and footwear, industrial & construction items have seen triple-digit growth in inspection demand from African importers,
		reflecting increasing infrastructure demands in Africa.
	</p>
	<p>
		Data suggests a more mature and organized supply chain based on the type of quality control services being requested by African importers sourcing from China.
		Comparing the first half of 2011 to the first half of 2012, African demand for Factory Audits, an ISO 9000 assessment of a factory, has more than tripled.
		Intriguing figures as audits are nearly always performed either when a new supplier is selected, or a new commitment for production is placed.
	</p>
</div>
<div class="head-small">
	<h2>Win-Win Sino-African Trade: Africa Emerging as a World Manufacturing Zone</h2>
</div>
<div class="para">
	<p>
		Trade between China and Africa is not only a one way street for “Made-in-China” goods.
		Analysts argue that in 2012 China will become the single largest export destination for Africa,
		a status driven mainly by natural resource exports (fuel, ores, and metal) that are ultimately used in the production of goods.
	</p>
	<p>
		In growing its export market to more than just natural resources, data from the Organisation for Economic Co-operation & Development (OECD) shows a 90%
		al growth in exports of Africa-manufactured products over the last decade. China’s share as a destination market grew by more than 200%;
		while traditional export markets for Africa, such as the EU and US, have seen their share decline by 20% over the same period.
	</p>
	<p>
		This data corroborates the trends witnessed by AsiaInspection; more and more importers sourcing from Asia are expressing a need for quality control in Africa.
		"Over the past few years at AsiaInspection, we have witnessed the rise of African sourcing in China as we helped African importers structure their supply chain in Asia”
		said Sebastien Breteau, CEO of AsiaInspection. “Even though the figures are still comparatively small, we’ve seen data to support that Africa is preparing to become the next
		sourcing frontier for manufactured goods - which is why we launched our quality control services there.”
	</p>
