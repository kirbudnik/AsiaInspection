<h1>中国在2012年持续发展</h1>
<p><b>中国深圳，2013年1月15日</b>AsiaInspection是行业领先的质量控制服务供应商，主要为从亚洲进口的企业提供服务。AsiaInspection于今日发表2012年第四季度报表，回顾了亚洲地区制造和质量控制产业在过去一年的发展。</p>
<p>以下是2012年制造和质量控制产业发生的一些重大新闻：</p>
<h2 style="font-size:18px;">2012年的亚洲制造业：尽管全球经济低迷，但出口需求仍然强劲</h2>
<div style="width:306px;float:left;text-align:left;">
	尽管全球经济低迷，但数据显示亚洲——特别是中国的出口市场依然很强劲。2012年前三个季度，中国的出口增加至占全球贸易额的11.1%，较去年同期增长0.6%。
	<br/><br/>
	2012年AsiaInspection的 <a href="http://www.asiainspection.com/quality-control-services-china-india-asia#product_inspections">执行检验</a>增长数据也证实了这一趋势，发展最快的<a href="http://www.asiainspection.com/quality-control-vietnam">越南</a>增长率达+34%，其次是孟加拉（+21%），接着是中国（+13%）和印度（+10%）。
	<br/><br/>
	尽管中国13%的增长率显得较为温和，但其出口占到这四个国家合计总额的84%（2012年九月至十一月，占四国$6450亿美金出口总额中的$5410亿美金）。“在这种基数水平上持续实现两位数增长，显示了中国在全球制造业中继续处于主导位置”，AsiaInspection的总裁施柏滔先生说道。 
