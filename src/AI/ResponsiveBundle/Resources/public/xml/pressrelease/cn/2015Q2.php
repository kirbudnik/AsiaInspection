<h1>6800万儿童被迫工作，每15秒钟都有人因工死亡</h1> 
 
<p><b>香港，2015年4月7日 – AsiaInspection (AI) 作为一家全球领先的质量控制和合规服务供应商，与全球品牌商、零售商和进口商合作，以建立、管理并优化供应链。AI 于今日发布了2015年第二季度报表，涵盖外包生产和质量控制服务行业的季度提要。</b> 
 
