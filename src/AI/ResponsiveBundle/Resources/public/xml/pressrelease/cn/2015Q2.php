<p style="text-align:justify;">在3月初举行的G7会议上，政府代表和相关方为即将到来的G7峰会设定了建立可持续、合乎道德的全球供应链的目标<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">1</sup></a>。 国际劳工组织发布消息指出，每15秒钟都有人死于工伤或职业病<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">1</sup></a>。随着全球对道德采购问题的关注度急剧上升，AsiaInspection第一季度道德审核需求同比增长65%。</p> 
<p style="text-align:justify;">如果G7峰会开始全面推广可持续全球供应链，品牌商和进口商将面临确保遵守这些国际标准的压力。以法国为例，3月末，法国通过了一项法案，该法案将有可能强制所有大型企业通过包括分包商在内的供应链履行社会责任。</p> 
<p style="text-align:justify;">处理道德责任问题时的一个主要挑战是供应链的日益复杂化。纽约大学商务和人权中心副主任Sarah Labowitz指出，未经授权的分包现象在孟加拉国仍然十分严重，对买家和消费者来说都缺乏透明度<a href="#footnotes" style="text-decoration: none;"><sup style="font-size: 10px;">2</sup></a>。AsiaInspection 就此提出具体的解决方案：为所有道德审核设定GPS 坐标，与实际生产地挂钩。</p> 
 
<h2 style="font-size:18px;">全球供应链如何进行水资源管理</h2> 
