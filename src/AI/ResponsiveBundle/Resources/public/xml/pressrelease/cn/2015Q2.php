<p style="text-align:justify;">全球供应链的环境意识正在提高：尽管关注点仍在劳动条件上，但最新数据显示品牌商和零售商正开始认真对待采购策略的环境影响。在世界水日前夕发表的简讯中，供应商商业道德信息交流 (Sedex) 公司分析了在Sedex Global 平台上注册的2000个审核的数据。简讯中介绍了供应链中与水资源相关的风险。Sedex成员在对供应商场地进行审核过程中发现，42%存在缺乏资源利用和处置许可、废物处理系统不完善、缺乏根据相关法规和客户需求检查环境绩效系统的问题。提出这些问题后，AsiaInspection 发现在第一季度中，买方对于环境审核和废水检测有了更大的兴趣。</p> 
 
<h2 style="font-size:18px;">越南和缅甸会成为下一个竞争者吗？</h2> 
<p>尽管从绝对数量上来说，中国仍然是最大的货源地，AsiaInspection 的数据表明，品牌商和零售商越来越倾向于从邻国获取货源。据全球调查显示，从2014年的第一季度到2015年的第一季度，中国货源占有率下降了12个百分点。</p> 
<p>根据最新的采购经理人指数，越南制造业的产量和新订单量都有所上升<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">3</sup></a>。相较于中国和泰国，越南拥有更年轻的人群和更低的工资水平，因而吸引了更多大品牌。今年1月，美国从中国的进口量自2008年以来首次下降， 越南则成了对美国出口量最大的东盟国家<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">4</sup></a>。与此同时，缅甸的服装制造业取得了长足发展，去年产业规模增长了3亿美元，并在三年内实现了产业规模翻倍<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">5</sup></a>。</p> 
<p>AI的数据证实，越南和缅甸执行的检测同比分别增长了190%和113%，增长速度惊人。另外，AI数据还表明，这一增长并没有以牺牲质量为代价。第一季度，越南生产的产品质量高于中国：中国产品的不合格率是28%，而越南产品的不合格率为24%。</p> 
 
<h2 style="font-size:18px;">中国和泰国的出口产品受到食品丑闻的冲击</h2> 
<p>亚洲的制造食品产量和食品丑闻都在持续增加。今年2月，澳大利亚从中国进口的一批速冻混合莓导致肝炎大规模泛滥。同样在2月，泰国生产的金枪鱼罐头导致了组胺中毒事件。澳大利亚总理Tony Abbott强调，若有国家想要出口到澳大利亚，一定要保证产品的安全<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">7</sup></a>。</p> 
<p>AsiaFoodInspection所执行的检测服务同比增长64%。中国出口食品的不合格率出现了11个月以来的首次下降，从去年第四季度的48%下降到了今年第一季度的41%。尽管这些还不足以证明全球范围内的食品安全状况已有所改善，但这些令人鼓舞的迹象证明，进口国正变得越来越小心谨慎。</p> 
 
