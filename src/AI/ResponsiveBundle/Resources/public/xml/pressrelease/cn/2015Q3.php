<p style="text-align:justify;"> 尽管中国的经济增长速度可能会在近六年内持续低迷，但世界品牌正向更多亚洲低成本国家扩大供应链。通过对比2014年7月至2015年6月与2013年7月至2014年6月的数据，AsiaInspection在报告中指出，中国的产品检测率稳健增长了7个百分点——与中国的国内生产总值增长率持平。同时报告指出，东南亚国家的增长率十分强劲。柬埔寨的产品检测率年增长幅度达到了前所未有的<b>227%</b>；越南则是<b>172%</b>；缅甸在最近几年内放宽了进口限额，并且废除了出口关税，增幅为<b>133%</b>；印度尼西亚为<b>114%</b>；泰国为<b>102%</b>；孟加拉国为<b>81%</b>；而此前默默无闻的巴基斯坦增幅也达到<b>47%</b>。 
