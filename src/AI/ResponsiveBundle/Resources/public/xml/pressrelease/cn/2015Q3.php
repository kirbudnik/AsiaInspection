<p style="text-align:justify;"> 虽然东南亚国家的增幅看似为服装出口所驱动，但也有一些值得注意的例外。AI最常检测的产品是来自印度尼西亚、马来西亚和菲律宾的家具；越南的鞋类，泰国的箱包以及韩国的音像设备。</p> 
 
<p style="text-align:justify;">“东南亚的总体发展趋势以及除中国之外国家的发展速度是史无前例的。”AI首席执行官Sebastien Breteau说道。“虽然近年来上升趋势十分明显，但是AI却看到了前所未有的市场需求。两年前，中国的产品检测占到了总数的91%，如今它所占比例为78%。”虽然一些世界品牌可能继续会和中国已有的一些工厂合作，不过数据显示它们可能在寻找新的出路。</p> 
 
 
 
 
<h2 style="font-size:18px;"> 越南 - 经济持续增长，道德与合规值居亚洲首位 
</h2> 
<div class="above990DisplayNone" style="padding:0px 0px 15px 15px;width:270px;float:right;"> 
<center> 
<a href="https://s3.asiainspection.com/images/news/2015Q3img1.jpg" target="_blank"> <img src="https://s3.asiainspection.com/images/news/2015Q3img1.jpg" width="255" height="170"/></a> 
