<div style="clear:both;"></div> 
</div> 
<p>越南目前是世界第四大服装出口国<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">1</sup></a>并持续努力通过服装产业的大型扩张计划及多份自由贸易协定继续提高出口份额。这些贸易协定部分已经签署，还有部分正在协商（包括正在进行的跨太平洋伙伴关系谈判）。<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">2,</sup></a> <a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">3</sup></a>AI数据显示，越南不仅检测率同比增加了<b>172</b>%，道德审核分值也在亚洲四小龙中分值最高。在过去一年中，越南道德审核分值高达<b>6.7</b>分（满分10分），位于其后的印度达<b>6.6</b>，中国达<b>6.2</b>孟加拉国达<b>6.5</b>。</p> 
<p>越南所取得的进步得益于近年来劳动法的改进、工人质量的提高<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">4</sup></a>工人质量的提高及国家增强公民安全意识的努力。<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">5</sup></a>英特尔位于越南的装配工厂在发布道德审核分值时也显示了同样的趋势。<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">6</sup></a> </p> 
 
<h2 style="font-size:18px;">缅甸放宽贸易壁垒 推动经济增长</h2> 
<div class="above990DisplayNone" style="padding:0px 0px 15px 15px;width:270px;float:right;"> 
<center> 
<a href="https://s3.asiainspection.com/images/news/myanmar-inspection.jpg" target="_blank"><img src="https://s3.asiainspection.com/images/news/myanmar-inspection.jpg" width="255" height="170" /></a> 
<br> 
<span style="font-weight:bold; margin-top:8px;text-align:center;">缅甸工厂里的一位AI检测员</span> 
</center> 
<div style="clear:both;"></div> 
</div> 
<p style="text-align:justify;"> 缅甸服装业持续快速发展。2015至2016财年，服装行业的出口收入目标设定为20亿美元。仅在过去的两年里，服装行业已新增了10万个工厂，外商投资达90%<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">7</sup></a>。2009至2010年，外商投资只有3亿美元，而2014年至2015年，外商投资已增加至50亿美元，年增长率为75.5%。<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">8</sup></a> </p> 
<p style="text-align:justify;"> 有趣的是，根据现场检测显示的可接受质量水平结果，缅甸生产的产品质量大大超过了亚洲平均水平。在所有检测中，缅甸产品可接受质量水平容忍度为<b>22%</b>,优于柬埔寨的<b>25%</b>，印度的<b>28%</b>，中国的<b>32%</b>，以及孟加拉国的<b>37%</b>。随着买家谨慎进入缅甸市场，并不断采用严格管制，这一数据或许反映了近期的投资情况，以及在此基础上的新型基础设施和严格的监督机制。</p> 
 
 
<h2 style="font-size:18px;"> 缺少食品安全法规 致使每年220万人丧命</h2> 
 
<p>食品安全是今年世界健康日的主题。根据世界卫生组织的数据显示，每年有220万人死于由食物和水造成的传染性疾病。食品供应链的规模及复杂性大大增加了食品污染和疾病传播的风险，对人们健康及经济发展十分不利<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">9</sup></a>。近期出现的食品丑闻，如中国没收了800吨走私冻肉，再一次深刻表明了出台食品安全法规的重要性<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">10</sup></a>。</p> 
 
<p>中国修订后的食品安全法将于今年10月生效，该法律规定了更为严格的食品生产、运输和存储要求，加重了对违法行为的惩罚力度。</p> 
<p>由于食品产业对食品安全的关注度增高，在过去的一年中，AsiaFoodInspection观察到，和上一年相比，食品服务质量提高了49％，使得食品检测成为AI增长最高的一项服务。这些检测的最新结果显示，第二季度不合格率为<b>37%</b>,比第一季度（41％）略有改善。</p> 
 
<h2 style="font-size:18px;"> 东南亚国家加紧环境限制</h2> 
<p>随着各国制定和执行更加严格的环保法规，人们对环境问题的关注也日益凸显。印度环保部门命令关闭近900家没有安装污水处理设施的纺织单位，这一行为打乱了印度的供应链。而越南最近拒绝了价值2亿美元的投资提议，理由是认为该提议中的纺织和印染工厂“有害环境”<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">13,</sup></a>。与此同时，中国发布了水污染防治行动计划，相关部门发出警告，严厉打击非法排污和伪造监测数据的行为。<a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">13,</sup></a> <a href="#见脚注" style="text-decoration: none;"><sup style="font-size: 10px;">14</sup></a> </p> 
 
