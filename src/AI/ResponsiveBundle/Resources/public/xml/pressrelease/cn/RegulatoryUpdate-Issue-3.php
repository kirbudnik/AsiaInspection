<h2 style="font-size:18px;">ACLASS认证标志变更为ANAB标志</h2>
<p>美国国家认可委员会（ANSI-ASQ National Accreditation Board）已从之前的三种认证标志：ANAB，ACLASS和FQS转变为了单一认证标志ANAB。美国国家认可委员会是国际实验室认证联盟（ILAC）和亚太实验室认证联盟（APLAC）多边互认协议的签约方，在美国乃至国际上都受到认可。AI ANSECO的实验室将会把ANAB认证标志加入检验报告，作为我们更新的ISO 17025认证证书，并且认证范围也会随着新标志更新。</p>

