<h2 style="font-size:18px;">中国对儿童纺织品实行强制性标准</h2>
<p>2015年6月，GB 31701-2015“婴幼儿纺织品安全技术规范”成为中国的一项强制性标准，将于2016年6月1日生效，过渡期为两年（2016年6月1日至2018年5月31日）。在过渡期内，2016年6月1日前生产且符合相关标准的产品仍可在市场上销售。从2018年6月1日起，市场上销售的所有产品必须符合这一标准。</p>
<p>分类（依据年龄）：</p>

<ul>
	<li>婴幼儿纺织品：36个月及以下</li>
	<li>儿童纺织品：3岁－14岁</li>
</ul>

<p>安全级别分类：</p>

<ul>
	<li>根据不同要求，安全级别可分为A、B、C三类。A类表示最高要求，B类次之，C类表示基本要求。</li>
	<li>婴幼儿纺织品必须满足A类要求；直接接触皮肤的儿童纺织品必须满足B类要求；非直接接触皮肤的儿童纺织品至少必须满足C类要求。</li>
	<li>安全级别须在儿童衣服标签上加以标注，婴幼儿纺织品也应标明“婴幼儿产品”字样。</li>
	<li>消费者在购买时可以参考衣服标签上标注的安全级别。</li>
</ul>

<p>提高安全标准：</p>

<ul>
	<li>该标准进一步提高了GB18401《国家纺织产品基本安全技术规范》中规定的婴幼儿和儿童纺织品的安全要求。 </li>
	<li>化学物质：增加了对六种增塑剂和两种重金属（铅和镉）的要求。</li>
	<li>机械安全：对儿童头、颈、肩、腰等部位的童装拉带提出详细要求。不允许婴幼儿和儿童（7岁以下）的服装在头部和颈部缀有拉带。</li>
	<li>纺织品装饰：应符合规定的抗拉强度标准，不能带有尖角利边。</li>
	<li>增加了对阻燃剂的要求。</li>
</ul>