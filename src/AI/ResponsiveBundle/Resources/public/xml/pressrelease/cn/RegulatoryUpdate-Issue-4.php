<br /><p><strong>墨西哥纺织品标签标准现已生效</strong></p>
<p>2014年11月6日，墨西哥经济部在政府公报中公布了两项最新纺织品标签国家标准：NMX-A-2076-INNTEX-2013和NMX-A-6938-INNTEX-2013。这两项标准现已纳入墨西哥的官方标准NOM-004-SCFI-2006，将作为强制性标签要求，于2015年9月9日起开始生效。</p>
<p>墨西哥政府表示，将为制造商、进口商和经销商提供一定时间，以便按照NOM-004-SCFI-2006标准中的新要求对生产过程做出调整。期间，政府批准在该标准生效日期之前生产的国内合法产品进入市场。</p>
<p><u>NMX-A-2076-INNTEX-2013 “纺织品-化学纤维-通用名称”</u></p>
<p>该标准列出了用于标示目前在工业纺织品及其他生产过程中所使用的不同种类和属性的化学纤维的通用名称。“化学纤维”（有时也称作“人造纤维”）用于指代经人工加工制成的纤维制品，用于区分自然纤维材料。该标准符合国际标准ISO 2076: 2013 “纺织品-人造纤维-通用名称”。</p>
<p>该标准将取代之前的NMX-A-099-INNTEX-2007标准。</p>
<p><u>NMX-A-6938-INNTEX-2013 “纺织品-天然纤维-通用名称与定义”</u></p>
<p>根据纤维成分和产地，该标准提供了最常见的自然纤维的通用名称及定义。此外，还按照字母顺序提供了一份常用名称列表以及命名的相应标准。这项标准完全符合国际标准ISO 6938: 2012 “纺织品-自然纤维-通用名称和定义”。按照此标准， “lana”(毛) 或 “pelo”（发）等字眼现可加在某些动物纤维的通用名称之前。</p>
<p>该标准将取代之前的NMX-A-099-INNTEX-2007标准。</p>

