<br /><p><strong>更新——日本对纺织品中偶氮含量的限制规定</strong></p>
<p>2015年7月，日本卫生劳动福利部颁布第124条部长条例，将偶氮化合物定为危险物质，规定24种芳香胺类含量在气相色谱-质谱联用仪检测下不得超过30毫克/千克。</p>
<p>此范围内的家用产品包括：</p>
<ul>
	<li>使用偶氮染色的纺织品，如罩子、内衣、睡衣、袜子、帽子、床上用品、毛巾、浴室防滑垫等相关产品</li>
	<li>使用偶氮染色的皮制品或毛制品，如衣服、鸭舌帽、圆边帽等相关产品。</li>
</ul>
<p>24种芳香胺类已在<a href="http://www.ansecogroup.com/regrecap/5871/" target="_blank">行业新闻概要2015年5月第2期中列出。</a></p>

