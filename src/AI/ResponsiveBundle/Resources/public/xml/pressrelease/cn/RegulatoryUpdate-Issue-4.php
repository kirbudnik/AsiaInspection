<br /><p><strong><a href="http://www.mainelegislature.org/legis/statutes/38/title38ch16-Dsec0.html" target="_blank">缅因州《儿童产品法》有毒化学物质种类</a>更新</strong></p>
<p>2015年7月21日，缅因州环境保护部（DEP）例行发布了每三年公布一次的高度关注物质清单，删除了升格为优先化学品的和不符合高危化学品标准的化学物质。目前该清单上共有<a href="http://www.maine.gov/dep/safechem/highconcern/index.html" target="_blank">36种物质</a>。</p>
<p>此外，缅因州环境保护部认定甲醛为优先化学品，需经过报告才可用于特定儿童产品中。该条例于2015年7月26日生效，要求自该日起180天内进行报告。更多内容 <a href="http://www.ansecogroup.com/wp-content/uploads/2015/10/096c885-1.pdf" target="_blank">在此插入附件中的096c885.PDF文件</a></p>
