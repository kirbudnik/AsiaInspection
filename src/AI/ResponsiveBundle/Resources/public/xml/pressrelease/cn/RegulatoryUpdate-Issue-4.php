<br /><p><strong>美国消费品安全委员会（CPSC）撤销关于豁免未经抛光处理木料的重金属第三方检测的最终裁定</strong></p>
<p>美国消费品安全委员会（CPSC）此前公布一项最终裁定，判定未经抛光处理的木料所含重金属含量未超过玩具安全标准ASTM F963-11所列上限。该裁定原计划于2015年9月15日起生效，给予未经抛光处理的木料免检权，免除ASTM F963-11中所列的第三方重金属测试。然而，由于收到大量反对意见，消费品安全委员会决定于2015年9月10日撤销该项最终裁定，并不再就此设定评议期。</p>
<p>美国消费品安全委员会计划就其收到的反对意见单独发布一份公告。</p>
<br /><p><strong>消费品安全委员会就将有机卤化物阻燃剂列为“禁用有害物质”的立法申请征求意见</strong></p>
<p>2015年8月19日，美国消费品安全委员会就一项立法申请发布通告，就将添加有机卤化物阻燃剂的数类产品列为《联邦有害物质管理法》（FHSA）中“禁用有害物质”的申请征求意见。该项申请由地球正义环境法律事务所（Earthjustice）、美国消费者联盟（Consumer Federation of America）联合其他组织共同提出，他们认为这些阻燃剂被广泛用于特定种类的消费品中，并会从产品中释放出来、扩散到室内环境中。此外，他们还提出，由于其固有毒性，接触这些化学品可能会损害人体健康，扰乱生殖系统、神经系统、内分泌系统和免疫系统，甚至诱发癌症。</p>
<p>该立法申请提出禁止在软垫家具、儿童用品、儿童护理用品中使用有机卤化物阻燃剂，并禁止在玩具、床垫和电子产品的塑料包装中添加此类成分。美国消费品安全委员会将在2015年10月19日前继续听取有关该项立法申请的意见。更多信息见于<a href="https://www.federalregister.gov/a/2015-20454" target="_blank">联邦公告</a>。</p>
<br /><p><strong>美国消费品安全委员会与海关边境保护局（CBP）对贸易信息试行“单一窗口”电子归档系统，开通自愿报名</strong></p>
<p>美国消费品安全委员会与海关边境保护局(CBP)联手，计划试行“单一窗口”系统，对美国消费品安全委员会管制下的消费制成品的部分贸易数据进行电子归档，以评估该电子归档系统。“单一窗口”电子归档系统可用于简化贸易电子数据的收集、处理、共享和检查工作，使用该系统可使货物进出口过程中联邦机构所需文件的整理更为便捷。该试行工作的初衷是将所有受监管的进口产品的合格证书进行电子归档。然而，经与相关方协商，委员会已暂时将试行的范围限制在收集目标范围最小或执行力度最小的数据，以促进合法贸易流通，加强对不合格产品的监管力度。</p>
<p>有意者可于2015年10月5日前自愿报名。请<a href="https://federalregister.gov/a/2015-20707" target="_blank">点击此处</a>获取详细信息。</p>
<br /><p><strong>美国试验材料学会（ASTM）更新商用弹簧床标准</strong></p>
<p>美国试验材料学会（ASTM）已就商用弹簧床的组件、装配、使用和标签发布ASTM F381-15号标准安全规范。新规范的目的如下：</p>
<ol>
	<li>减轻使用弹簧床对消费者造成的危害；</li>
	<li>处理使用者在家庭环境中使用弹簧床的问题；以及</li>
	<li>明确该规范不适用于批量生产的弹簧床及在水上使用的弹簧床。</li>
</ol>
<p>不建议6岁以下儿童使用。

